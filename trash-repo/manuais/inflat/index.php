<H2 CLASS="western" ALIGN=JUSTIFY STYLE="font-style: normal"><A NAME="4.3.Atualiza&ccedil;&atilde;o de valores|outline"></A><A NAME="4.3.Atualiza&ccedil;&atilde;o de valores|outline"></A>
<A HREF="#Sum&aacute;rio1|region" NAME="db1682db">Atualiza&ccedil;&atilde;o
de valores</A></H2>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.04cm; margin-bottom: 0cm">
Essa rotina tem a funcionalidade de simular a atualiza&ccedil;&atilde;o
de um determinado valor, utilizando a base dos inflatores
cadastrados. A rotina n&atilde;o utiliza diretamente o inflator, e
sim uma receita, pois al&eacute;m da corre&ccedil;&atilde;o do valor,
&eacute; calculado tamb&eacute;m a multa e o juro sobre o valor
informado, considerando a data de opera&ccedil;&atilde;o e a data de
vencimento.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; margin-bottom: 0cm">
Para atualizar um determinado valor, preencha os campos abaixo
conforme figura 6.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><I><B>C&oacute;digo</B></I></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 1.96cm; margin-bottom: 0cm">
Quando gravar uma simula&ccedil;&atilde;o de atualiza&ccedil;&atilde;o
de valores, esse campo ser&aacute; gerado automaticamente e
corresponde ao c&oacute;digo do registro para pesquisa futura.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><I><B>Descri&ccedil;&atilde;o</B></I></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 1.85cm; margin-bottom: 0cm">
Digite uma descri&ccedil;&atilde;o que identifique esse registro.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><I><B>N&uacute;mero
do CGM</B></I></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; margin-bottom: 0cm">
Digite o c&oacute;digo do CGM ou clique no link para selecionar um
contribuinte que queira relacionar com a atualiza&ccedil;&atilde;o de
valores que est&aacute; sendo efetuada. Essa informa&ccedil;&atilde;o
s&oacute; &eacute; obrigat&oacute;rio caso deseje gravar o registro.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><I><B>Data
base</B></I></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; margin-bottom: 0cm">
Esse campo corresponde a data que deseja atualizar o valor.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><I><B>Data
valor</B></I></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 1.96cm; margin-bottom: 0cm">
Esse campo corresponde a data de opera&ccedil;&atilde;o do valor, ou
seja, a data onde o d&eacute;bito foi constitu&iacute;do.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><I><B>Data
Vencimento</B></I></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; margin-bottom: 0cm">
Esse campo corresponde a data que venceu o d&eacute;bito que est&aacute;
sendo atualizado. 
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><I><B>Valor</B></I></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; margin-bottom: 0cm">
Nesse campo deve ser lan&ccedil;ado o valor hist&oacute;rico do
d&eacute;bito que deseja fazer a atualiza&ccedil;&atilde;o.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><I><B>Observa&ccedil;&atilde;o</B></I></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.04cm; margin-bottom: 0cm">
Esse campo est&aacute; dispon&iacute;vel para digitar qualquer
observa&ccedil;&atilde;o a respeito do registro que est&aacute; sendo
lan&ccedil;ado. Essa informa&ccedil;&atilde;o n&atilde;o &eacute;
obrigat&oacute;ria.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><I><B>Receita</B></I></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.04cm; margin-bottom: 0cm">
Selecione a receita que quer vincular essa atualiza&ccedil;&atilde;o
de valor, pois a forma de atualiza&ccedil;&atilde;o (inflator que
ser&aacute; utilizado), percentual de multa e percentual de juros
mensal, est&aacute; vinculado &agrave; receita.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.04cm; margin-bottom: 0cm">
<BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.04cm; margin-bottom: 0cm">
Ap&oacute;s lan&ccedil;ar as informa&ccedil;&otilde;es descritas
acima, clique no bot&atilde;o calcular e ser&aacute; gerado um
registro conforme figura 7. Clique no bot&atilde;o imprimir para
gerar relat&oacute;rio com o valor atualizado. Clique no bot&atilde;o
gravar caso deseje salvar o registro.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.04cm; margin-bottom: 0cm">
Para selecionar uma atualiza&ccedil;&atilde;o realizada
anteriormente, clique no bot&atilde;o pesquisar e selecione o item
desejado na lista.</P>
