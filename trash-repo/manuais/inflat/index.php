<FONT FACE="Helvetica"><FONT SIZE=4><B>M&Oacute;DULO INFLATORES</B></FONT></FONT></P>
<P CLASS="western"><BR><BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 1.98cm"><FONT SIZE=3>Este
m&oacute;dulo foi desenvolvido com o objetivo de formar uma base de
dados com os indexadores utilizados para atualizar monetariamente os
d&eacute;bitos pendentes de pagamentos dos contribuintes.</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 1.98cm"><FONT SIZE=3>Dessa
forma, o munic&iacute;pio pode tanto registrar os valores dos
indexadores oficiais do Governo Federal como criar seus pr&oacute;prios
m&eacute;todos de atualiza&ccedil;&atilde;o de valores.</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 1.98cm"><FONT SIZE=3>Os
inflatores podem ser criados de duas formas:</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><FONT SIZE=3><B>1 &ndash;</B>
configurado para aplicar o fator de corre&ccedil;&atilde;o m&ecirc;s
a m&ecirc;s sobre o valor do d&eacute;bito a partir da data de
opera&ccedil;&atilde;o do respectivo valor.</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><FONT SIZE=3><B>2 &ndash;</B>
configurado para converter o valor a ser corrigido em quantidade de
inflatores considerando o valor do inflator registrado na data de
opera&ccedil;&atilde;o do d&eacute;bito e multiplicar pelo valor do
inflator da data atual.</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><BR>
</P>
<H1 CLASS="western" ALIGN=JUSTIFY><A NAME="1.Cadastro|outline"></A><A NAME="1.Cadastro|outline"></A>
Cadastro</H1>
<H2 CLASS="western" ALIGN=JUSTIFY STYLE="font-style: normal"><A NAME="1.1.Inflatores|outline"></A><A NAME="1.1.Inflatores|outline"></A>
<FONT SIZE=4><B><A HREF="#Sum&aacute;rio1|region" NAME="db1484db">Inflatores</A></B></FONT></H2>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 1.96cm; margin-bottom: 0cm">
<FONT SIZE=3>Para criar um novo inflator, acesse o menu: Cadastros
-&gt;Inflatores -&gt; Inclus&atilde;o, conforme figura 1.</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 1.96cm; margin-bottom: 0cm">
<BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 1.96cm; margin-bottom: 0cm">
<FONT SIZE=3><I><B><A HREF="#Sum&aacute;rio1|region" NAME="db1485db">Inclus&atilde;o</A></B></I></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><FONT SIZE=3><I><B>C&oacute;digo
do Inflator</B></I></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; margin-bottom: 0cm">
<FONT SIZE=3>Informar como c&oacute;digo, a sigla do inflator;</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 1.98cm; margin-bottom: 0cm">
<FONT SIZE=3><B>Ex.:</B> IPCA,UFIR, IGPM, REAL.</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><FONT SIZE=3><I><B>Descri&ccedil;&atilde;o
do Inflator</B></I></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; margin-bottom: 0cm">
<FONT SIZE=3>Informar o nome do inflator;</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; margin-bottom: 0cm">
<FONT SIZE=3><B>Ex.: </B>&Iacute;ndice Nacional de Pre&ccedil;os ao
Consumidor &ndash; IPCA</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; margin-bottom: 0cm">
<BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><FONT SIZE=3><I><B>Picture</B></I></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; margin-bottom: 0cm">
<FONT SIZE=3>Nesse campo &eacute; definido o formato do inflator, ou
seja, o n&uacute;mero de d&iacute;gitos a ser informado no valor;</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; margin-bottom: 0cm">
<FONT SIZE=3><B>Ex.:</B> 9999,9999 </FONT>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><FONT SIZE=3><I><B>Tipo
de lan&ccedil;amento</B></I></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; margin-bottom: 0cm">
<FONT SIZE=3>&Eacute; a periodicidade que o inflator ser&aacute;
atualizado, podendo ser di&aacute;ria, mensal ou anual;</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><FONT SIZE=3><I><B>Tipo
de inflator</B></I></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; margin-bottom: 0cm">
<FONT SIZE=3>Nesse campo, &eacute; definido como ser&aacute;
utilizado o inflator para corrigir um d&eacute;bito, podem ser
configurado das seguintes maneiras:</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><FONT SIZE=3>	<B>a)
</B>Divide o valor pelo &iacute;ndice da data base e multiplica pelo
&iacute;ndice da data atual. Com esse tipo, o d&eacute;bito ser&aacute;
convertido em uma quantidade de inflatores considerando o valor da
data de opera&ccedil;&atilde;o e convertido novamente para valor
considerando o valor do &iacute;ndice da data atual;</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; margin-bottom: 0cm">
<FONT SIZE=3><B>Ex.:</B>	valor do inflator em 01/01/2000 = R$ 1,50
(data base)</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 3.68cm; margin-bottom: 0cm">
<FONT SIZE=3>	valor do inflator em 01/01/2007= R$ 2,50 (data atual)</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><FONT SIZE=3>Nesse
exemplo, a atualiza&ccedil;&atilde;o de um d&eacute;bito de R$ 100,00
lan&ccedil;ado em 01/01/2000, para 01/01/2007 ser&aacute; da seguinte
forma: 100,00 / 1,50 X 2,50 = 250,00.</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><FONT SIZE=3>	<B>b)</B>
Aplica o &iacute;ndice m&ecirc;s a m&ecirc;s sobre o valor</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><FONT SIZE=3>Com
esse tipo, o d&eacute;bito ser&aacute; atualizado atrav&eacute;s da
aplica&ccedil;&atilde;o do &iacute;ndice do inflator acumulado m&ecirc;s
a m&ecirc;s desde a data de opera&ccedil;&atilde;o do d&eacute;bito
at&eacute; a data atual;</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; margin-bottom: 0cm">
<FONT SIZE=3><B>Ex.:</B>	Valor do inflator em 01/01/2007 = 0,50 %</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 3.78cm; margin-bottom: 0cm">
<FONT SIZE=3>Valor do inflator em 01/02/2007 = 0,45 %</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 3.78cm; margin-bottom: 0cm">
<FONT SIZE=3>Valor do inflator em 01/03/2007 = 0,55 %</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; margin-bottom: 0cm">
<FONT SIZE=3>Nesse exemplo, a atualiza&ccedil;&atilde;o de um d&eacute;bito
de R$ 100,00 lan&ccedil;ado em 01/01/2007 para 01/03/2007 ser&aacute;
da seguinte forma: 100,00 X 1,50 % = 1,50 + 100,00 = 101,50.</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"> 
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><FONT SIZE=3>	<B>c)
</B>Aplica o &iacute;ndice do m&ecirc;s do vencimento sobre o valor</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><FONT SIZE=3>Com
esse tipo, o d&eacute;bito ser&aacute; atualizado atrav&eacute;s da
aplica&ccedil;&atilde;o do &iacute;ndice definido na data do
vencimento do d&eacute;bito;</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; margin-bottom: 0cm">
<FONT SIZE=3><B>Ex.:</B>	Valor do inflator em 01/01/2007 = 0,50 %</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 3.76cm; margin-bottom: 0cm">
<FONT SIZE=3>Valor do inflator em 01/02/2007 = 0,45 %</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 3.76cm; margin-bottom: 0cm">
<FONT SIZE=3>Valor do inflator em 01/03/2007 = 0,55 %</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 1.98cm; margin-bottom: 0cm">
<FONT SIZE=3>Nesse exemplo, a atualiza&ccedil;&atilde;o de um d&eacute;bito
de R$ 100,00 lan&ccedil;ado em 01/01/2007 e com vencimento em
01/02/2007 para 01/03/2007 ser&aacute; da seguinte forma: 100,00 X
0,45 % = 0,45 + 100,00 = 100,45. Nesse caso ser&aacute; utilizado
para corre&ccedil;&atilde;o somente o &iacute;ndice apurado na data
que venceu o d&eacute;bito.</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><BR>
</P>
