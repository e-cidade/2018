<P STYLE="text-indent: 0.79in; margin-bottom: 0in">M&Oacute;DULO 
</P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in">FARM&Aacute;CIA</P>
<P ALIGN=CENTER><BR><BR>
</P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in">O objetivo deste
m&oacute;dulo &eacute; fazer a entrega dos medicamentos nos postos de
distribui&ccedil;&atilde;o de medicamentos, permitindo o farmac&ecirc;utico
fazer a classifica&ccedil;&atilde;o dos medicamentos e parametrizar
os Tipos de Receita.</P>
<P ALIGN=JUSTIFY><BR><BR>
</P>
<H1 CLASS="western"><A NAME="1.Cadastros|outline"></A><A CLASS="western" HREF="#Sum&aacute;rio1_Head|region" NAME="db3470db">Cadastros</A></H1>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in">Nesta op&ccedil;&atilde;o
tem-se acesso a todos os cadastros do Modulo Farm&aacute;cia, como o
cadastro de Medicamentos, Tipos de Receita e Classifica&ccedil;&atilde;o
de Medicamentos. &Eacute; importante deixa-los sempre bem
atualizados.</P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><BR>
</P>
<H2 CLASS="western"><A NAME="1.1.Medicamentos|outline"></A><A CLASS="western" HREF="#Sum&aacute;rio1_Head|region" NAME="db6886db">Medicamentos</A></H2>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in">Este cadastro
consiste em vincular os itens do M&oacute;dulo Materiais com os
Medicamentos do M&oacute;dulo Farm&aacute;cia e fazer a classifica&ccedil;&atilde;o
dos medicamentos, conforme o seu principio ativo, sendo esta somente
as classifica&ccedil;&otilde;es anal&iacute;ticas do cadastro. 
</P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><BR>
</P>
<H3 CLASS="western"><A NAME="1.1.1.Inclus&atilde;o|outline"></A><A CLASS="western" HREF="#Sum&aacute;rio1_Head|region" NAME="db6887db">Inclus&atilde;o</A></H3>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>C&oacute;digo:
</B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">O
c&oacute;digo do medicamento ser&aacute; gerado automaticamente e
corresponde ao c&oacute;digo do item que est&aacute; sendo inclu&iacute;do.</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Nome
Gen&eacute;rico: </B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Neste
campo deve ser digitado o nome gen&eacute;rico do medicamento a ser
cadastrado.</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Medicamento:
</B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Neste
campo deve ser digitado o c&oacute;digo do medicamento a ser
cadastrado. Esse &eacute; o c&oacute;digo cadastrado no m&oacute;dulo
Material. Ou pode-se clicar no link e fazer a pesquisa pelo nome. </SPAN></SPAN>
</P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Medicamento
Anvisa:</B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Neste
campo vincularemos o medicamento a ser cadastrado a um medicamento da
Anvisa. (Usado para medicamentos controlados/continuados).</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Prescri&ccedil;&atilde;o
M&eacute;dica: </B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Neste
campo selecionaremos a prescri&ccedil;&atilde;o M&eacute;dica do
medicamento. (Usado para medicamentos controlados/continuados).</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Classe
Terap&ecirc;utica:</B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">
Neste campo selecionaremos a classe Terap&ecirc;utica do medicamento.
(Usado para medicamentos controlados/continuados).</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Lista
Controlados: </B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Neste
campo vincularemos o medicamento a uma lista de controlados. (Usado
para medicamentos controlados/continuados).</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Medicamento
Refer&ecirc;ncia: </B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Neste
campo selecionaremos o medicamento de Refer&ecirc;ncia do medicamento
a ser cadastrado. (Usado para medicamentos controlados/continuados).</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Laborat&oacute;rio</B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">:
Neste campo vincularemos o medicamento a um Laborat&oacute;rio.
(Usado para medicamentos controlados/continuados).</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Forma</B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">
</SPAN></SPAN><B><I>Farmac&ecirc;utica</I></B><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">:
Neste campo vincularemos o medicamento a uma forma </SPAN></SPAN><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Farmac&ecirc;utica.
(Usado para medicamentos controlados/continuados).</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Concentra&ccedil;&atilde;o</B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">:
Neste campo vincularemos o medicamento a uma concentra&ccedil;&atilde;o.
(Usado para medicamentos controlados/continuados).</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Classifica&ccedil;&atilde;o</B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">:
Neste campo vincularemos o medicamento a uma classifica&ccedil;&atilde;o.</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Observa&ccedil;&atilde;o</B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">:
Neste campo pode-se descrever uma observa&ccedil;&atilde;o para o
medicamento a ser cadastrado.</SPAN></SPAN></P>
<P STYLE="margin-bottom: 0in"><IMG SRC="Manual_Farmacia_html_601d5536.png" NAME="graphics1" ALIGN=LEFT WIDTH=605 HEIGHT=323 BORDER=0><BR>
</P>
<H3 CLASS="western"><A NAME="1.1.2.Altera&ccedil;&atilde;o|outline"></A>
<A CLASS="western" HREF="#Sum&aacute;rio1_Head|region" NAME="db6888db">Altera&ccedil;&atilde;o</A></H3>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in">Na altera&ccedil;&atilde;o
do cadastro de medicamento n&atilde;o pode ser alterado o item do
material,  caso o medicamento possua uma sa&iacute;da do estoque. O
campo Classifica&ccedil;&atilde;o n&atilde;o possui nenhuma
restri&ccedil;&atilde;o, igualmente o de Observa&ccedil;&atilde;o.  
</P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><BR>
</P>
<H3 CLASS="western"><A NAME="1.1.3.Exclus&atilde;o|outline"></A><A CLASS="western" HREF="#Sum&aacute;rio1_Head|region" NAME="db6889db">Exclus&atilde;o</A></H3>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in">Na exclus&atilde;o
n&atilde;o poder&aacute; ser exclu&iacute;do nenhum medicamento que
possuir alguma movimenta&ccedil;&atilde;o no sistema.</P>
<P STYLE="margin-bottom: 0in"><BR>
</P>
<P STYLE="margin-bottom: 0in"><BR>
</P>
