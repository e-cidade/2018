<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><B><I>CGS: </I></B><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Colocar
o c&oacute;digo do CGS (Paciente)  ou clicar no link para pesquisar o
nome do paciente.</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"> <I><B>Tipo de
retirada:</B></I></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Receita:</B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">
Selecionar se a receita foi gerada pelo sistema, neste caso, se n&atilde;o
foi gerada pelo sistema o usu&aacute;rio deve preencher os dados da
receita. Se foi gerara pelo sistema, ele busca o c&oacute;digo da
receita que deve ser atendido pelo atendente (Solicita&ccedil;&atilde;o).</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Tipo de
Receita:</B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">
Selecionar o tipo de receita referente a entrega do medicamento para
os pacientes. Ao selecionar um tipo que o par&acirc;metro est&aacute;
com Requisitante obrigat&oacute;rio o sistema abre os campos
referente ao requisitante.</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Receita:</B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">
Colocar o n&uacute;mero da receita caso ela seja manual e possuir uma
numera&ccedil;&atilde;o.</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Validade:</B>
</I><SPAN STYLE="font-style: normal">Colocar a</SPAN><I> </I><SPAN STYLE="font-style: normal">validade
da receita m&eacute;dica.</SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>N&uacute;mero
da notifica&ccedil;&atilde;o:</B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">
Colocar o n&uacute;mero da notifica&ccedil;&atilde;o informado na
receita azul (s&oacute; &eacute; necess&aacute;rio colocar a n&uacute;mero
da notifica&ccedil;&atilde;o se for selecionado tipo da receita
azul).</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Profissional:
</B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Colocar
o c&oacute;digo do profissional respons&aacute;vel pela receita
m&eacute;dica ou clicar no link do Profissional para pesquisar pelo
nome.</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Cadastro de
profissionais:</B></I></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Medicamento:
</B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Colocar
o c&oacute;digo do medicamento ou clicar no link Medicamento e
pesquisar pelo nome. Depois de selecionado o sistema mostrar&aacute;
o saldo do medicamento.</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Continuados:</B></I></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Quantidade
dispensada: </B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Lan&ccedil;ar
a quantidade do item que se quer retirar, neste caso, somente a
quantidade dispon&iacute;vel no campo saldo.</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Ver Lote:
</B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Este
campo s&oacute; &eacute; visualizado quando o medicamento est&aacute;
vinculando a um material que controla validade do item.  Caso o
usu&aacute;rio n&atilde;o olhe o lote, o sistema retira a quantidade
do lote que vence primeiro. Se clicar em ver lote, o usu&aacute;rio
pode escolher qual lote quer ser retirado.</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Saldo: </B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Neste
campo o usu&aacute;rio pode visualizar o saldo do medicamento em
estoque.</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Posologia:
</B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Colocar
a posologia prescrita pelo m&eacute;dico na receita.</SPAN></SPAN></P>
