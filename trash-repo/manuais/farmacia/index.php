<H2 CLASS="western"><A NAME="4.5.Par&acirc;metros|outline"></A><A CLASS="western" HREF="#Sum&aacute;rio1_Head|region" NAME="db6894db">Par&acirc;metros</A></H2>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in">Nesta op&ccedil;&atilde;o
&eacute; configurado os par&acirc;metros para o m&oacute;dulo de
Farm&aacute;cia no sistema. 
</P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><BR>
</P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><BR>
</P>
<P STYLE="margin-bottom: 0in"><I><B>Campos</B></I></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>C&oacute;digo:
</B></I><FONT COLOR="#000000"><FONT SIZE=3><SPAN LANG="zxx"><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">O
c&oacute;digo de classifica&ccedil;&atilde;o ser&aacute; gerado
automaticamente e corresponde ao c&oacute;digo do item que est&aacute;
sendo inclu&iacute;do.</SPAN></SPAN></SPAN></FONT></FONT></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Estrutura:
</B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Este
campo busca a estrutura dos n&iacute;veis cadastrados no M&oacute;dulo
Configura&ccedil;&otilde;es.</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Descri&ccedil;&atilde;o:
</B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Colocar
o nome da estrutura nos par&acirc;metros.</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Digita&ccedil;&atilde;o
Medicamentos: </B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Selecionar
a SIM para digita&ccedil;&atilde;o manual dos medicamentos em uma
receita (solicita&ccedil;&atilde;o) feita pelo DBPortal.</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Med.
Controlado:</B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">
Este par&acirc;metro controla a distribui&ccedil;&atilde;o dos
Medicamentos Controlados por paciente. Permite o administrador
escolher 3 (tr&ecirc;s) op&ccedil;&otilde;es:</SPAN></SPAN></P>
<UL>
	<LI><P STYLE="margin-bottom: 0in"><I><B>N&atilde;o Notifica:</B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">
	Nesta op&ccedil;&atilde;o o sistema executa uma distribui&ccedil;&atilde;o
	de medicamento normal, sem olhar o cadastro de Medicamentos
	Controlados.</SPAN></SPAN></P>
	<LI><P STYLE="margin-bottom: 0in"><I><B>Notifica e Bloqueia: </B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Nesta
	op&ccedil;&atilde;o o sistema ao fazer um distribui&ccedil;&atilde;o
	de </SPAN></SPAN>
	</P>
	<LI><P STYLE="margin-bottom: 0in; font-style: normal; font-weight: normal">
	</P>
	<P STYLE="margin-bottom: 0in"></P>
	<LI><P STYLE="margin-bottom: 0in; font-style: normal; font-weight: normal">
	um medicamento controlado, ele verifica se o paciente possui saldo
	dispon&iacute;vel para a retirada em um determinado per&iacute;odo e
	bloqueia a retirada caso ele n&atilde;o possua.</P>
	<LI><P STYLE="margin-bottom: 0in"><I><B>Notifica e N&atilde;o
	Bloqueia: </B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Nesta
	op&ccedil;&atilde;o o sistema ao fazer um distribui&ccedil;&atilde;o
	de um medicamento controlado, ele verifica se o paciente possui
	saldo dispon&iacute;vel para a retirada em um determinado per&iacute;odo
	mas n&atilde;o bloqueia a retirada.</SPAN></SPAN></P>
</UL>
