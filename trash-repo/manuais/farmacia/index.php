<H2 CLASS="western"><A NAME="4.3.Medicamento Controlados|outline"></A>
<A CLASS="western" HREF="#Sum&aacute;rio1_Head|region" NAME="db6990db">Medicamento
Controlados</A></H2>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in">Este procedimento
realiza o cadastro dos medicamentos controlados por paciente,
permitindo ao administrador controlar os ciclos de entrega por
medicamento, cada um &eacute; controlado separadamente na hora da
Entrega do Medicamento.</P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in">Esta rotina faz
toda valida&ccedil;&atilde;o na Entrega do Medicamento, pois verifica
o saldo dos medicamentos dentro de uma freq&uuml;&ecirc;ncia de
retiradas do paciente e n&atilde;o diretamente no saldo do
medicamento.</P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><BR>
</P>
<P STYLE="margin-bottom: 0in"><I><B>Campos</B></I></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>CGS: </B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Colocar
o c&oacute;digo do CGS (Paciente)  ou clicar no link para pesquisar o
nome.</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Medicamento:
</B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Colocar
o c&oacute;digo do Medicamento ou clicar no link para pesquisar o
nome.</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Quantidade:
</B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Neste
campo deve ser preenchido com a quantidade de medicamento que o
paciente retira dentro de uma freq&uuml;&ecirc;ncia.</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Freq&uuml;&ecirc;ncia:
</B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Neste
campo deve ser preenchido o per&iacute;odo de retirada do
medicamento, neste caso, deve ser em dias.</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Margem:
</B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Neste
campo deve ser preenchido os dias de toler&acirc;ncia de cada
medicamento.</SPAN></SPAN></P>
<P ALIGN=JUSTIFY STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Programa:
 </B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Colocar
 o  programa  em   que o  medicamento  controlado  pertence.</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Observa&ccedil;&atilde;o:
</B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Colocar
as observa&ccedil;&otilde;es que ser&atilde;o impressas na
Carteirinha do paciente.</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Validade</B></I></P>
<UL>
	<LI><P STYLE="margin-bottom: 0in"><I><B>Inicio: </B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Data
	de inicio do controle de retirada do medicamento.</SPAN></SPAN></P>
	<LI><P STYLE="margin-bottom: 0in"><I><B>Fim: </B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Data
	de fim do controle de retirada do medicamento.</SPAN></SPAN></P>
</UL>
