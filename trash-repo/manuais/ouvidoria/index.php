<BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY><FONT FACE="Arial, sans-serif"><I><B>Filtros</B></I></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="font-style: normal; font-weight: normal">
<FONT FACE="Arial, sans-serif">	<FONT COLOR="#000000">Informe a
descri&ccedil;&atilde;o do tipo, a validade (se este tiver prazo
determinado a vencer) e se este exigir&aacute; identifica&ccedil;&atilde;o
do cidad&atilde;o, conforme mostra afigura tipo de processo.</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><FONT COLOR="#000000"><FONT FACE="Arial, sans-serif"><I><B>	</B></I><SPAN STYLE="font-style: normal"><B>Tipo
de Processo: </B></SPAN><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">O
c&oacute;digo do tipo ser&aacute; gerado automaticamente pelo
sistema.</SPAN></SPAN></FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><FONT COLOR="#000000"><FONT FACE="Arial, sans-serif"><I><B>	</B></I><SPAN STYLE="font-style: normal"><B>Descri&ccedil;&atilde;o:</B></SPAN>
Descrever<SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">
o nome dos tipos de processos que ser&atilde;o utilizados no
atendimento ao cidad&atilde;o na abertura do processo, ou seja, um
cadastro gen&eacute;rico dos tipos de reclama&ccedil;&otilde;es,
denuncias, elogios, sugest&otilde;es que a ouvidoria recebe ao
realizar o atendimento ao cidad&atilde;o.</SPAN></SPAN></FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><FONT COLOR="#000000"><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">	</SPAN></SPAN><SPAN STYLE="font-style: normal"><B>Ex.:</B></SPAN><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">
Ilumina&ccedil;&atilde;o P&uacute;blica (troca de lampadas
