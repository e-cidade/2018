realizada com sucesso, clique no bot&atilde;o OK, em seguida o
sistema perguntar&aacute; se o usu&aacute;rio deseja arquivar o
processo deste atendimento, para arquivar, confirme clicando no bot&atilde;o
OK, automaticamente o processo ser&aacute; arquivado com a observa&ccedil;&atilde;o
&uml;processo finalizado, atendimento vinculado a outro processo&uml;,
