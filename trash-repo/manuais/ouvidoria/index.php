<H2 CLASS="western"><FONT COLOR="#000080"><FONT FACE="Arial, sans-serif">Desarquivamento</FONT></FONT></H2>
<P CLASS="western" ALIGN=JUSTIFY STYLE="font-weight: normal"><BR><BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="font-weight: normal"><FONT COLOR="#000000"><FONT FACE="Arial, sans-serif"><FONT SIZE=3>	Atrav&eacute;s
desta rotina o usu&aacute;rio poder&aacute; reativar no sistema um
processo arquivado, ao acessar a rotina o sistema abrir&aacute; uma
lookup com todos os processos arquivados no departamento em que o
usu&aacute;rio esta logado, ser&aacute; disponibilizado ainda os
alguns filtros para que o usu&aacute;rio possa filtrar a pesquisa, de
modo que possa simplificar sua consulta.</FONT></FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><FONT FACE="Arial, sans-serif"><I><B>Filtros
de pesquisa</B></I></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><FONT FACE="Arial, sans-serif">	<FONT COLOR="#000000"><B>C&oacute;digo
do Processo:</B></FONT><FONT COLOR="#000000"><SPAN STYLE="font-weight: normal">
Campo num&eacute;rico destinado ao c&oacute;digo do processo,
possuindo este c&oacute;digo, o usu&aacute;rio poder&aacute; realizar
uma busca direta ao processo, onde, ao clicar no bot&atilde;o
pesquisar ap&oacute;s informar o c&oacute;digo, o sistema carregar&aacute;
automaticamente os dados deste processo para desarquivamento.</SPAN></FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><FONT COLOR="#000000"><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-weight: normal">	</SPAN><B>Requerente:</B><SPAN STYLE="font-weight: normal">
Campo alfanum&eacute;rico destinado ao nome do requerente, se o
usu&aacute;rio souber o nome do requerente do processo a ser
desarquivado, poder&aacute; digitar neste campo e clicar em
pesquisar, o sistema carregar&aacute; todos os processos que est&atilde;o
arquivados no departamento em que o usu&aacute;rio esta logado do
requerente pesquisado.</SPAN></FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><FONT COLOR="#000000"><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-weight: normal">	</SPAN><B>Data
do Arquivamento:</B><SPAN STYLE="font-weight: normal"> O sistema
disponibiliza dois campos data, o primeiro destinado a data inicial
do per&iacute;odo a ser pesquisado e o segundo destinado a data
final, utilizando estes campos, o sistema carregar&aacute; todos os
processos arquivado no departamento em que o usu&aacute;rio esta
logado dentro do per&iacute;odo informado, se o usu&aacute;rio
informar apenas a data inicial, o sistema carregar&aacute; todos os
processos arquivados a partir da data informada, se informado apenas
a data final, o sistema trar&aacute; todos os processo arquivados no
departamento at&eacute; a data informada.</SPAN></FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><FONT COLOR="#000000"><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-weight: normal">	</SPAN><B>Tipo
de Processo:</B><SPAN STYLE="font-weight: normal"> Campo combobox com
a rela&ccedil;&atilde;o de todos os tipos de processos do m&oacute;dulo
ouvidoria, utilizando este campo, o usu&aacute;rio poder&aacute;
filtrar os registros da pesquisa por tipo de processo, ou seja, o
sistema listar&aacute; neste campo todos os processos arquivados no
departamento do tipo de processo informado.</SPAN></FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="font-weight: normal"><FONT FACE="Arial, sans-serif">	</FONT><FONT COLOR="#000000"><FONT FACE="Arial, sans-serif">Obs.:
N&atilde;o &eacute; obrigat&oacute;rio o uso de todos os filtros para
realizar uma pesquisa, o usu&aacute;rio poder&aacute; utilizar um ou
mais filtros simultaneamente.</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="font-weight: normal"><FONT COLOR="#000000"><FONT FACE="Arial, sans-serif">	Ap&oacute;s
selecionar o processo que ser&aacute; desarquivado o sistema
carregar&aacute; os dados do mesmo para a tela desarquivamento de
processo, nesta o usu&aacute;rio poder&aacute; confirmar se o
processo selecionado &eacute; o processo a ser desarquivado, conforme
mostra na figura desarquivamento, clicando no bot&atilde;o [
desarquivar ] o sistema ativar&aacute; o processo novamente para
tramite, disponibilizando o mesmo na rotina controle de atendimento
para que qualquer usu&aacute;rio do departamento possa tramita-lo,
clicando em pesquisar, o sistema exibir&aacute; novamente a lookup de
pesquisa.</FONT></FONT></P>
