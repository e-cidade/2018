<H2 CLASS="western">Cadastro do tipo de processo</H2>
<P CLASS="western" STYLE="margin-bottom: 0cm"><BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm; font-style: normal; text-decoration: none">
<FONT FACE="Arial, sans-serif"><FONT SIZE=3>	<FONT COLOR="#000000">Nesse
menu s&atilde;o cadastrados os tipos de processos para o controle dos
processos de ouvidoria. N&atilde;o pode ser criado um processo no
sistema sem um tipo de processo e o mesmo deve estar vinculado a um
andamento padr&atilde;o (departamentos em que o processo ir&aacute;
tramitar). &Eacute; no tipo que s&atilde;o vinculados os documentos
exigidos pela ouvidoria para abrir um processo.</FONT></FONT></FONT></P>
