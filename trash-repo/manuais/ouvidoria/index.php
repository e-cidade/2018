<H3 CLASS="western"><FONT COLOR="#000080">Inclus&atilde;o</FONT></H3>
<P CLASS="western" ALIGN=JUSTIFY STYLE="font-weight: normal"><FONT COLOR="#000000"><FONT FACE="Arial, sans-serif">O
Atendimento &eacute; uma rotina que permite ao Ouvidor incluir no
sistema as demandas surgidas por outros meios de contato do cidad&atilde;o
com a institui&ccedil;&atilde;o.</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><FONT COLOR="#000000"><FONT FACE="Arial, sans-serif">Atrav&eacute;s
desta rotina o ouvidor ir&aacute; registrar todas as reclama&ccedil;&otilde;es,
solicita&ccedil;&otilde;es, duvidas, elogios, den&uacute;ncias e
sugest&otilde;es realizadas pelos cidad&atilde;os do munic&iacute;pio
atrav&eacute;s das formas de reclama&ccedil;&atilde;o (pessoalmente,
carta, email ou telefone/fax), estas ser&atilde;o habilitadas ap&oacute;s
ser informado o tipo de processo, pois o ouvidor poder&aacute;
configurar as formas em que o cidad&atilde;o poder&aacute; reclamar,
ou seja, de acordo com o tipo de processo, o sistema habilitar&aacute;
as formas de reclama&ccedil;&atilde;o, usando como exemplo uma
den&uacute;ncia grave que exija que o cidad&atilde;o realize a mesma
pessoalmente, por se tratar de algo que envolver&aacute; auditorias
ou inspe&ccedil;&otilde;es, e estas n&atilde;o poder&atilde;o ser
baseadas em denuncias an&ocirc;nimas, com poss&iacute;vel
apresenta&ccedil;&atilde;o de provas.</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><FONT COLOR="#000080">	</FONT><FONT COLOR="#000000"><FONT FACE="Arial, sans-serif">O
tratamento de uma manifesta&ccedil;&atilde;o s&atilde;o os
procedimentos necess&aacute;rios para o Ouvidor poder indicar ao
cidad&atilde;o a melhor solu&ccedil;&atilde;o para o seu caso, bem
como classificar cada manifesta&ccedil;&atilde;o recebida de modo a
possibilitar dados gerenciais para os dirigentes p&uacute;blicos.
Sendo assim, &eacute; importante descrever claramente a manifesta&ccedil;&atilde;o
do cidadao ou extrair deste o maximo de informacoes possiveis que
possam auxiliar, em seguida informar o tipo de processo para que o
sistema possa calcular a data estimada em que sera dado o retorno ao
cidadao como tambem se para o tipo de processo selecionado, sera
exigido a identificacao do cidadao, pesquisando seu cadastro no
cadastro de cidadaos ou CGM, confirmando os dados para ter absoluta
certeza se o cadastro selecionado pertence realmente ao cidadao. Em
seguida, pergunte ao mesmo se este deseja receber retorno quando seu
processo for concluido, obtendo uma resposta</FONT></FONT><FONT COLOR="#000080">
</FONT><FONT COLOR="#000000"><FONT FACE="Arial, sans-serif">afirmativa,
pergunte qual a melhor forma de retorno, pessoalmente, via telefone,
carta ou e-mail.</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><SPAN STYLE="font-weight: normal">	</SPAN><FONT COLOR="#000000"><FONT FACE="Arial, sans-serif"><SPAN STYLE="font-weight: normal">Se
o atendimento realizado trata-se de uma manifestacao j&aacute;
realizada por outro cidadao ou este mesmo cidadao tornou a
manifestar-se pelo mesmo motivo, n&atilde;o sera necessario gerar
outro processo, utilizando o campo processos existentes &eacute;
possivel anexar o novo atendimento ao processo que j&aacute; esta
tramitando.</SPAN></FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="font-weight: normal"><FONT COLOR="#000000"><FONT FACE="Arial, sans-serif">	Caso
seja gerado um novo processo, o sistema permite fazer a correcao,
realizando o arquivamento do processo gerado e vinculando o
atendimento deste ao processo em tramitacao, para isto, deve-se usar
a rotina vincular atendimento a processo, busque o processo que sera
arquivado, faca a exclusao do atendimento do mesmo, o sistema ira
perguntar se o processo sera arquivado, isto pode ser feito neste
momento ou em outra ocasiao atraves da rotina arquivamento, em
seguida busque o processo em que sera vinculado o atendimento,
selecione o mesmo e inclua.</FONT></FONT></P>
