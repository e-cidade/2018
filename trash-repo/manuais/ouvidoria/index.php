setores p&uacute;blicos tenham os dados do cidad&atilde;o reclamante
ou se para o tipo de processo, estes dados s&atilde;o irrelevantes,
n&atilde;o obrigat&oacute;rio a identifica&ccedil;&atilde;o do
cidad&atilde;o.</FONT></FONT></FONT></P>
