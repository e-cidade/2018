   // funcao para atualizar campos
   function atualizacampos($exclusao=false) {
     if($exclusao==false){
       if($this->k13_data == ""){
         $this->k13_data_dia = ($this->k13_data_dia == ""?@$GLOBALS["HTTP_POST_VARS"]["k13_data_dia"]:$this->k13_data_dia);
