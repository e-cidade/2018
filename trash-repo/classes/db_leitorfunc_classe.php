   function sql_query ( $oid = null,$campos="leitorfunc.oid,*",$ordem=null,$dbwhere=""){ 
     $sql = "select ";
     if($campos != "*" ){
       $campos_sql = split("#",$campos);
       $virgula = "";
       for($i=0;$i<sizeof($campos_sql);$i++){
         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }else{
       $sql .= $campos;
     }
     $sql .= " from leitorfunc ";
     $sql .= "      inner join leitor  on  leitor.bi10_codigo = leitorfunc.bi12_leitor";
     $sql .= "      inner join rechumano  on  rechumano.ed20_i_codigo = leitorfunc.bi12_rechumano";
     $sql .= "      inner join leitorcategoria  on  leitorcategoria.bi07_codigo = leitor.bi10_leitorcategoria";
     $sql .= "      inner join rhpessoal  on  rhpessoal.rh01_regist = rechumano.ed20_i_codigo";
     $sql2 = "";
     if($dbwhere==""){
       if( $oid != "" && $oid != null){
          $sql2 = " where leitorfunc.oid = '$oid'";
       }
     }else if($dbwhere != ""){
       $sql2 = " where $dbwhere";
     }
     $sql .= $sql2;
     if($ordem != null ){
       $sql .= " order by ";
       $campos_sql = split("#",$ordem);
       $virgula = "";
       for($i=0;$i<sizeof($campos_sql);$i++){
         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }
     return $sql;
