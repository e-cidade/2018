      $this->atualizacampos();
     $sql = " update levret set ";
     $virgula = "";
     if(trim($this->y64_sequencia)!="" || isset($GLOBALS["HTTP_POST_VARS"]["y64_sequencia"])){ 
       $sql  .= $virgula." y64_sequencia = $this->y64_sequencia ";
       $virgula = ",";
       if(trim($this->y64_sequencia) == null ){ 
         $this->erro_sql = " Campo Sequencial nao Informado.";
         $this->erro_campo = "y64_sequencia";
         $this->erro_banco = "";
         $this->erro_msg   = "Usuário: \\n\\n ".$this->erro_sql." \\n\\n";
         $this->erro_msg   .=  str_replace('"',"",str_replace("'","",  "Administrador: \\n\\n ".$this->erro_banco." \\n"));
         $this->erro_status = "0";
         return false;
       }
     }
     if(trim($this->y64_inscr)!="" || isset($GLOBALS["HTTP_POST_VARS"]["y64_inscr"])){ 
       $sql  .= $virgula." y64_inscr = $this->y64_inscr ";
       $virgula = ",";
       if(trim($this->y64_inscr) == null ){ 
         $this->erro_sql = " Campo Inscricao nao Informado.";
         $this->erro_campo = "y64_inscr";
         $this->erro_banco = "";
         $this->erro_msg   = "Usuário: \\n\\n ".$this->erro_sql." \\n\\n";
         $this->erro_msg   .=  str_replace('"',"",str_replace("'","",  "Administrador: \\n\\n ".$this->erro_banco." \\n"));
         $this->erro_status = "0";
         return false;
       }
     }
     $sql .= " where ";
     if($y64_sequencia!=null){
       $sql .= " y64_sequencia = $this->y64_sequencia";
     }
     $resaco = $this->sql_record($this->sql_query_file($this->y64_sequencia));
     if($this->numrows>0){
       for($conresaco=0;$conresaco<$this->numrows;$conresaco++){
         $resac = db_query("select nextval('db_acount_id_acount_seq') as acount");
         $acount = pg_result($resac,0,0);
         $resac = db_query("insert into db_acountacesso values($acount,".db_getsession("DB_acessado").")");
         $resac = db_query("insert into db_acountkey values($acount,5038,'$this->y64_sequencia','A')");
