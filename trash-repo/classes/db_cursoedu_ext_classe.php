require_once("db_cursoedu_classe.php");
class cl_cursoedu_ext extends class cl_curso {
   function sql_query_cursodiario ( $ed31_i_codigo=null,$campos="*",$ordem=null,$dbwhere=""){
     $sql = "select ";
     if($campos != "*" ){
       $campos_sql = split("#",$campos);
       $virgula = "";
       for($i=0;$i<sizeof($campos_sql);$i++){
         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }else{
       $sql .= $campos;
     }
     $sql .= " from cursoedu ";
     $sql .= "  inner join cursoescola on ed71_i_curso = ed29_i_codigo";
     $sql .= "  inner join ensino on ed10_i_codigo = ed29_i_ensino";
     $sql .= "  inner join base on ed31_i_curso = ed29_i_codigo";
     $sql .= "  inner join turma on ed57_i_base = ed31_i_codigo";
     $sql .= "  inner join matricula on ed60_i_turma = ed57_i_codigo";
     $sql2 = "";
     if($dbwhere==""){
       if($ed31_i_codigo!=null ){
         $sql2 .= " where base.ed31_i_codigo = $ed31_i_codigo ";
       }
     }else if($dbwhere != ""){
       $sql2 = " where $dbwhere";
     }
     $sql .= $sql2;
     if($ordem != null ){
       $sql .= " order by ";
       $campos_sql = split("#",$ordem);
       $virgula = "";
       for($i=0;$i<sizeof($campos_sql);$i++){
         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }
     return $sql;
  }
}
