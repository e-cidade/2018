           $resac = db_query("insert into db_acount values($acount,128,694,'".AddSlashes(pg_result($resaco,$conresaco,'d03_descr'))."','$this->d03_descr',".db_getsession('DB_datausu').",".db_getsession('DB_id_usuario').")");
       }
