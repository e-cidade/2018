   //funcao construtor da classe 
   function cl_editaltipo() { 
     //classes dos rotulos dos campos
     $this->rotulo = new rotulo("editaltipo"); 
     $this->pagina_retorno =  basename($GLOBALS["HTTP_SERVER_VARS"]["PHP_SELF"]);
   }
   //funcao erro 
   function erro($mostra,$retorna) { 
     if(($this->erro_status == "0") || ($mostra == true && $this->erro_status != null )){
        echo "<script>alert(\"".$this->erro_msg."\");</script>";
        if($retorna==true){
           echo "<script>location.href='".$this->pagina_retorno."'</script>";
        }
     }
   }
   // funcao para atualizar campos
   function atualizacampos($exclusao=false) {
     if($exclusao==false){
       $this->d03_tipos = ($this->d03_tipos == ""?@$GLOBALS["HTTP_POST_VARS"]["d03_tipos"]:$this->d03_tipos);
       $this->d03_descr = ($this->d03_descr == ""?@$GLOBALS["HTTP_POST_VARS"]["d03_descr"]:$this->d03_descr);
     }else{
       $this->d03_tipos = ($this->d03_tipos == ""?@$GLOBALS["HTTP_POST_VARS"]["d03_tipos"]:$this->d03_tipos);
     }
   }
   // funcao para inclusao
   function incluir ($d03_tipos){ 
      $this->atualizacampos();
     if($this->d03_descr == null ){ 
       $this->erro_sql = " Campo Descricao do Serviço nao Informado.";
       $this->erro_campo = "d03_descr";
       $this->erro_banco = "";
       $this->erro_msg   = "Usuário: \\n\\n ".$this->erro_sql." \\n\\n";
       $this->erro_msg   .=  str_replace('"',"",str_replace("'","",  "Administrador: \\n\\n ".$this->erro_banco." \\n"));
       $this->erro_status = "0";
       return false;
     }
     if($d03_tipos == "" || $d03_tipos == null ){
       $result = db_query("select nextval('editaltipo_d03_tipos_seq')"); 
       if($result==false){
         $this->erro_banco = str_replace("\n","",@pg_last_error());
         $this->erro_sql   = "Verifique o cadastro da sequencia: editaltipo_d03_tipos_seq do campo: d03_tipos"; 
         $this->erro_msg   = "Usuário: \\n\\n ".$this->erro_sql." \\n\\n";
         $this->erro_msg   .=  str_replace('"',"",str_replace("'","",  "Administrador: \\n\\n ".$this->erro_banco." \\n"));
         $this->erro_status = "0";
         return false; 
       }
       $this->d03_tipos = pg_result($result,0,0); 
     }else{
       $result = db_query("select last_value from editaltipo_d03_tipos_seq");
       if(($result != false) && (pg_result($result,0,0) < $d03_tipos)){
         $this->erro_sql = " Campo d03_tipos maior que último número da sequencia.";
         $this->erro_banco = "Sequencia menor que este número.";
         $this->erro_msg   = "Usuário: \\n\\n ".$this->erro_sql." \\n\\n";
         $this->erro_msg   .=  str_replace('"',"",str_replace("'","",  "Administrador: \\n\\n ".$this->erro_banco." \\n"));
         $this->erro_status = "0";
         return false;
       }else{
         $this->d03_tipos = $d03_tipos; 
       }
     }
     if(($this->d03_tipos == null) || ($this->d03_tipos == "") ){ 
       $this->erro_sql = " Campo d03_tipos nao declarado.";
       $this->erro_banco = "Chave Primaria zerada.";
       $this->erro_msg   = "Usuário: \\n\\n ".$this->erro_sql." \\n\\n";
       $this->erro_msg   .=  str_replace('"',"",str_replace("'","",  "Administrador: \\n\\n ".$this->erro_banco." \\n"));
       $this->erro_status = "0";
       return false;
     }
     $sql = "insert into editaltipo(
                                       d03_tipos 
                                      ,d03_descr 
                       )
                values (
                                $this->d03_tipos 
                               ,'$this->d03_descr' 
                      )";
     $result = db_query($sql); 
     if($result==false){ 
       $this->erro_banco = str_replace("\n","",@pg_last_error());
       if( strpos(strtolower($this->erro_banco),"duplicate key") != 0 ){
         $this->erro_sql   = " ($this->d03_tipos) nao Incluído. Inclusao Abortada.";
         $this->erro_msg   = "Usuário: \\n\\n ".$this->erro_sql." \\n\\n";
         $this->erro_banco = " já Cadastrado";
         $this->erro_msg   .=  str_replace('"',"",str_replace("'","",  "Administrador: \\n\\n ".$this->erro_banco." \\n"));
       }else{
         $this->erro_sql   = " ($this->d03_tipos) nao Incluído. Inclusao Abortada.";
         $this->erro_msg   = "Usuário: \\n\\n ".$this->erro_sql." \\n\\n";
         $this->erro_msg   .=  str_replace('"',"",str_replace("'","",  "Administrador: \\n\\n ".$this->erro_banco." \\n"));
       }
       $this->erro_status = "0";
       $this->numrows_incluir= 0;
       return false;
     }
     $this->erro_banco = "";
     $this->erro_sql = "Inclusao efetuada com Sucesso\\n";
         $this->erro_sql .= "Valores : ".$this->d03_tipos;
     $this->erro_msg   = "Usuário: \\n\\n ".$this->erro_sql." \\n\\n";
     $this->erro_msg   .=  str_replace('"',"",str_replace("'","",  "Administrador: \\n\\n ".$this->erro_banco." \\n"));
     $this->erro_status = "1";
     $this->numrows_incluir= pg_affected_rows($result);
     $resaco = $this->sql_record($this->sql_query_file($this->d03_tipos));
     if(($resaco!=false)||($this->numrows!=0)){
       $resac = db_query("select nextval('db_acount_id_acount_seq') as acount");
       $acount = pg_result($resac,0,0);
       $resac = db_query("insert into db_acountacesso values($acount,".db_getsession("DB_acessado").")");
       $resac = db_query("insert into db_acountkey values($acount,693,'$this->d03_tipos','I')");
       $resac = db_query("insert into db_acount values($acount,128,693,'','".AddSlashes(pg_result($resaco,0,'d03_tipos'))."',".db_getsession('DB_datausu').",".db_getsession('DB_id_usuario').")");
       $resac = db_query("insert into db_acount values($acount,128,694,'','".AddSlashes(pg_result($resaco,0,'d03_descr'))."',".db_getsession('DB_datausu').",".db_getsession('DB_id_usuario').")");
     }
     return true;
   } 
