   function sql_query ( $k71_codigo=null,$campos="*",$ordem=null,$dbwhere=""){ 
     $sql = "select ";
     if($campos != "*" ){
       $campos_sql = split("#",$campos);
       $virgula = "";
       for($i=0;$i<sizeof($campos_sql);$i++){
         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }else{
       $sql .= $campos;
     }
     $sql .= " from recreparcdest ";
     $sql .= "      inner join tabrec  on  tabrec.k02_codigo = recreparcdest.k71_recdest";
     $sql .= "      inner join recreparcori  on  recreparcori.k70_codigo = recreparcdest.k71_codigo";
     $sql .= "      inner join tabrecjm  on  tabrecjm.k02_codjm = tabrec.k02_codjm";
     $sql .= "      inner join tabrec  on  tabrec.k02_codigo = recreparcori.k70_recori";
     $sql2 = "";
     if($dbwhere==""){
       if($k71_codigo!=null ){
         $sql2 .= " where recreparcdest.k71_codigo = $k71_codigo "; 
       } 
     }else if($dbwhere != ""){
       $sql2 = " where $dbwhere";
     }
     $sql .= $sql2;
     if($ordem != null ){
       $sql .= " order by ";
       $campos_sql = split("#",$ordem);
       $virgula = "";
       for($i=0;$i<sizeof($campos_sql);$i++){
         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }
     return $sql;
  }
