       $resac = db_query("insert into db_acount values($acount,1702,9954,'','".AddSlashes(pg_result($resaco,0,'k37_descr'))."',".db_getsession('DB_datausu').",".db_getsession('DB_id_usuario').")");
     }
     return true;
   } 
