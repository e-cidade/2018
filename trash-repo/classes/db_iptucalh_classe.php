   //funcao erro 
   function erro($mostra,$retorna) { 
     if(($this->erro_status == "0") || ($mostra == true && $this->erro_status != null )){
        echo "<script>alert(\"".$this->erro_msg."\");</script>";
        if($retorna==true){
           echo "<script>location.href='".$this->pagina_retorno."'</script>";
        }
     }
   }
   // funcao para atualizar campos
   function atualizacampos($exclusao=false) {
     if($exclusao==false){
       $this->j17_codhis = ($this->j17_codhis == ""?@$GLOBALS["HTTP_POST_VARS"]["j17_codhis"]:$this->j17_codhis);
       $this->j17_descr = ($this->j17_descr == ""?@$GLOBALS["HTTP_POST_VARS"]["j17_descr"]:$this->j17_descr);
     }else{
       $this->j17_codhis = ($this->j17_codhis == ""?@$GLOBALS["HTTP_POST_VARS"]["j17_codhis"]:$this->j17_codhis);
     }
   }
   // funcao para inclusao
   function incluir ($j17_codhis){ 
      $this->atualizacampos();
     if($this->j17_descr == null ){ 
       $this->erro_sql = " Campo Descrição nao Informado.";
       $this->erro_campo = "j17_descr";
       $this->erro_banco = "";
       $this->erro_msg   = "Usuário: \\n\\n ".$this->erro_sql." \\n\\n";
       $this->erro_msg   .=  str_replace('"',"",str_replace("'","",  "Administrador: \\n\\n ".$this->erro_banco." \\n"));
       $this->erro_status = "0";
       return false;
     }
     if($j17_codhis == "" || $j17_codhis == null ){
       $result = db_query("select nextval('iptucalh_j17_codhis_seq')"); 
       if($result==false){
         $this->erro_banco = str_replace("\n","",@pg_last_error());
         $this->erro_sql   = "Verifique o cadastro da sequencia: iptucalh_j17_codhis_seq do campo: j17_codhis"; 
