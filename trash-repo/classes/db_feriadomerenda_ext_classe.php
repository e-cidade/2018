require_once("db_feriado_classe.php");

class cl_feriadomerenda_ext extends cl_feriado  { 
function sql_query_merenda ( $ed54_i_codigo=null,$campos="*",$ordem=null,$dbwhere=""){ 
     $sql = "select ";
     if($campos != "*" ){
       $campos_sql = split("#",$campos);
       $virgula = "";
       for($i=0;$i<sizeof($campos_sql);$i++){
         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }else{
       $sql .= $campos;
     }
     $sql .= " from feriado ";
     $sql .= "      inner join calendario  on  calendario.ed52_i_codigo = feriado.ed54_i_calendario";
     $sql .= "      inner join calendarioescola on calendarioescola.ed38_i_calendario = calendario.ed52_i_codigo";
     $sql .= "      inner join evento  on  evento.ed96_i_codigo = feriado.ed54_i_evento";
     $sql .= "      inner join duracaocal  on  duracaocal.ed55_i_codigo = calendario.ed52_i_duracaocal";
     $sql2 = "";
     if($dbwhere==""){
       if($ed54_i_codigo!=null ){
         $sql2 .= " where feriado.ed54_i_codigo = $ed54_i_codigo "; 
       } 
     }else if($dbwhere != ""){
       $sql2 = " where $dbwhere";
     }
     $sql .= $sql2;
     if($ordem != null ){
       $sql .= " order by ";
       $campos_sql = split("#",$ordem);
       $virgula = "";
       for($i=0;$i<sizeof($campos_sql);$i++){
         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }
     return $sql;
  }
}
