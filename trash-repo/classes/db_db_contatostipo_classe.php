           $resac = db_query("insert into db_acount values($acount,1317,7854,'".AddSlashes(pg_result($resaco,$conresaco,'g02_descr'))."','$this->g02_descr',".db_getsession('DB_datausu').",".db_getsession('DB_id_usuario').")");
       }
