       }
     }else{
       $sql .= $campos;
     }
     $sql .= " from mensagemnotificacaotipo ";
     $sql2 = "";
     if($dbwhere==""){
       if($db133_sequencial!=null ){
         $sql2 .= " where mensagemnotificacaotipo.db133_sequencial = $db133_sequencial "; 
       } 
