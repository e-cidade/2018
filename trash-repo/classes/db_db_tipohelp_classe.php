           $resac = db_query("insert into db_acount values($acount,651,4840,'".AddSlashes(pg_result($resaco,$conresaco,'descrtipohelp'))."','$this->descrtipohelp',".db_getsession('DB_datausu').",".db_getsession('DB_id_usuario').")");
