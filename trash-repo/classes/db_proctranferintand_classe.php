     }
     $sql .= " from proctranferintand ";
     $sql .= "      inner join procandam  on  procandam.p61_codandam = proctranferintand.p87_codandam";
     $sql .= "      inner join proctranferint  on  proctranferint.p88_codigo = proctranferintand.p87_codtransferint";
