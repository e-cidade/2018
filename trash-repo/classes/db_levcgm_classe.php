         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }else{
       $sql .= $campos;
     }
     $sql .= " from levcgm ";
     $sql .= "      inner join cgm  on  cgm.z01_numcgm = levcgm.y93_numcgm";
     $sql .= "      inner join levanta  on  levanta.y60_codlev = levcgm.y93_codlev";
     $sql2 = "";
     if($dbwhere==""){
       if($y93_codlev!=null ){
         $sql2 .= " where levcgm.y93_codlev = $y93_codlev "; 
       } 
     }else if($dbwhere != ""){
       $sql2 = " where $dbwhere";
