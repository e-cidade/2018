     if($dbwhere==""){
       if($it33_sequencial!=null ){
         $sql2 .= " where itbilocalidaderural.it33_sequencial = $it33_sequencial "; 
       } 
     }else if($dbwhere != ""){
       $sql2 = " where $dbwhere";
     }
     $sql .= $sql2;
     if($ordem != null ){
       $sql .= " order by ";
