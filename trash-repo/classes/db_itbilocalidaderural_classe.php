     }
     $sql .= " from itbilocalidaderural ";
     $sql .= "      inner join itbi  on  itbi.it01_guia = itbilocalidaderural.it33_guia";
     $sql .= "      inner join localidaderural  on  localidaderural.j137_sequencial = itbilocalidaderural.it33_localidaderural";
     $sql .= "      inner join db_usuarios  on  db_usuarios.id_usuario = itbi.it01_id_usuario";
