   // funcao para atualizar campos
   function atualizacampos($exclusao=false) {
     if($exclusao==false){
       $this->ed324_sequencial = ($this->ed324_sequencial == ""?@$GLOBALS["HTTP_POST_VARS"]["ed324_sequencial"]:$this->ed324_sequencial);
