         $this->erro_status = "0";
         return false; 
       }
       $this->ed324_sequencial = pg_result($result,0,0); 
     }else{
       $result = db_query("select last_value from tipovinculo_ed324_sequencial_seq");
       if(($result != false) && (pg_result($result,0,0) < $ed324_sequencial)){
         $this->erro_sql = " Campo ed324_sequencial maior que último número da sequencia.";
         $this->erro_banco = "Sequencia menor que este número.";
         $this->erro_msg   = "Usuário: \\n\\n ".$this->erro_sql." \\n\\n";
         $this->erro_msg   .=  str_replace('"',"",str_replace("'","",  "Administrador: \\n\\n ".$this->erro_banco." \\n"));
         $this->erro_status = "0";
         return false;
       }else{
         $this->ed324_sequencial = $ed324_sequencial; 
       }
     }
     if(($this->ed324_sequencial == null) || ($this->ed324_sequencial == "") ){ 
       $this->erro_sql = " Campo ed324_sequencial nao declarado.";
       $this->erro_banco = "Chave Primaria zerada.";
