       $this->erro_sql   = "Erro ao selecionar os registros.";
       $this->erro_msg   = "Usuário: \\n\\n ".$this->erro_sql." \\n\\n";
       $this->erro_msg   .=  str_replace('"',"",str_replace("'","",  "Administrador: \\n\\n ".$this->erro_banco." \\n"));
       $this->erro_status = "0";
       return false;
     }
     $this->numrows = pg_numrows($result);
      if($this->numrows==0){
        $this->erro_banco = "";
        $this->erro_sql   = "Record Vazio na Tabela:recparprocdiver";
        $this->erro_msg   = "Usuário: \\n\\n ".$this->erro_sql." \\n\\n";
        $this->erro_msg   .=  str_replace('"',"",str_replace("'","",  "Administrador: \\n\\n ".$this->erro_banco." \\n"));
        $this->erro_status = "0";
        return false;
      }
     return $result;
   }
   function sql_query ( $procdiver=null,$campos="*",$ordem=null,$dbwhere=""){ 
     $sql = "select ";
     if($campos != "*" ){
       $campos_sql = split("#",$campos);
       $virgula = "";
       for($i=0;$i<sizeof($campos_sql);$i++){
         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }else{
       $sql .= $campos;
     }
     $sql .= " from recparprocdiver ";
     $sql .= "      inner join tabrec      on  tabrec.k02_codigo 				= recparprocdiver.receita";
     $sql .= "      inner join procdiver   on  procdiver.dv09_procdiver = recparprocdiver.procdiver";
     $sql .= "      inner join tabrecjm    on  tabrecjm.k02_codjm       = tabrec.k02_codjm";
     $sql .= "      inner join histcalc    on  histcalc.k01_codigo      = procdiver.dv09_hist";
     $sql .= "      inner join tabrec as t on  t.k02_codigo 		        = procdiver.dv09_receit";
     $sql .= "      inner join arretipo    on  arretipo.k00_tipo        = procdiver.dv09_tipo";
     $sql .= "      inner join proced      on  proced.v03_codigo   			= procdiver.dv09_proced";
     $sql2 = "";
     if($dbwhere==""){
       if($procdiver!=null ){
         $sql2 .= " where recparprocdiver.procdiver = $procdiver "; 
       } 
     }else if($dbwhere != ""){
       $sql2 = " where $dbwhere";
     }
     $sql .= $sql2;
     if($ordem != null ){
       $sql .= " order by ";
       $campos_sql = split("#",$ordem);
       $virgula = "";
       for($i=0;$i<sizeof($campos_sql);$i++){
         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }
     return $sql;
  }
