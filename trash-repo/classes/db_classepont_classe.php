   function sql_query ( $q25_classe=null,$campos="*",$ordem=null,$dbwhere=""){ 
     $sql = "select ";
     if($campos != "*" ){
       $campos_sql = split("#",$campos);
       $virgula = "";
       for($i=0;$i<sizeof($campos_sql);$i++){
         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }else{
       $sql .= $campos;
     }
     $sql .= " from classepont ";
     $sql .= "      inner join classe   on  classe.q12_classe = classepont.q25_classe  ";
     $sql .= "       left join clasativ on classepont.q25_classe = clasativ.q82_classe ";     
     $sql2 = "";
     if($dbwhere==""){
       if($q25_classe!=null ){
         $sql2 .= " where classepont.q25_classe = $q25_classe "; 
       } 
     }else if($dbwhere != ""){
       $sql2 = " where $dbwhere";
     }
     $sql .= $sql2;
     if($ordem != null ){
       $sql .= " order by ";
       $campos_sql = split("#",$ordem);
       $virgula = "";
       for($i=0;$i<sizeof($campos_sql);$i++){
         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }
     return $sql;
  }
