       $result = @pg_query("select last_value from orctipoproj_o38_tipoproj_seq");
       if(($result != false) && (pg_result($result,0,0) < $o38_tipoproj)){
         $this->erro_sql = " Campo o38_tipoproj maior que último número da sequencia.";
         $this->erro_banco = "Sequencia menor que este número.";
