     }
   }
   // funcao para inclusao
   function incluir ($bi03_codigo){ 
      $this->atualizacampos();
     if($this->bi03_classificacao == null ){ 
       $this->erro_sql = " Campo Classificação Literária nao Informado.";
       $this->erro_campo = "bi03_classificacao";
       $this->erro_banco = "";
       $this->erro_msg   = "Usuário: \\n\\n ".$this->erro_sql." \\n\\n";
       $this->erro_msg   .=  str_replace('"',"",str_replace("'","",  "Administrador: \\n\\n ".$this->erro_banco." \\n"));
       $this->erro_status = "0";
       return false;
     }
     if($bi03_codigo == "" || $bi03_codigo == null ){
       $result = db_query("select nextval('classiliteraria_bi03_codigo_seq')"); 
       if($result==false){
         $this->erro_banco = str_replace("\n","",@pg_last_error());
         $this->erro_sql   = "Verifique o cadastro da sequencia: classiliteraria_bi03_codigo_seq do campo: bi03_codigo"; 
