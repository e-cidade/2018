       $virgula = "";
       for($i=0;$i<sizeof($campos_sql);$i++){
         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }else{
       $sql .= $campos;
     }
     $sql .= " from tesinteroutros ";
     $sql .= "      inner join tesinter  on  tesinter.j39_sequencial = tesinteroutros.j84_tesinter";
     $sql .= "      inner join tesintertipo  on  tesintertipo.j92_sequencial = tesinteroutros.j84_tesintertipo";
     $sql .= "      inner join lote  on  lote.j34_idbql = tesinter.j39_idbql";
     $sql .= "      inner join orientacao  on  orientacao.j64_sequencial = tesinter.j39_orientacao";
     $sql2 = "";
     if($dbwhere==""){
       if( $oid != "" && $oid != null){
          $sql2 = " where tesinteroutros.oid = '$oid'";
       }
     }else if($dbwhere != ""){
       $sql2 = " where $dbwhere";
     }
