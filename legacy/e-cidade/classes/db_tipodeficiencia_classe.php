     $sql .= "  from tipodeficiencia ";
     $sql2 = "";
     if (empty($dbwhere)) {
       if (!empty($rh150_sequencial)) {
         $sql2 .= " where tipodeficiencia.rh150_sequencial = $rh150_sequencial "; 
