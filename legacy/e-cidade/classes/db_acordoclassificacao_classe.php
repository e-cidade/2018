       }
     }else{
       $sql .= $campos;
     }
     $sql .= " from acordoclassificacao ";
     $sql2 = "";
     if($dbwhere==""){
       if($ac46_sequencial!=null ){
         $sql2 .= " where acordoclassificacao.ac46_sequencial = $ac46_sequencial "; 
       } 
     }else if($dbwhere != ""){
       $sql2 = " where $dbwhere";
     }
     $sql .= $sql2;
