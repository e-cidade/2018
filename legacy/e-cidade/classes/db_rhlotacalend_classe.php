         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }else{
       $sql .= $campos;
     }
     $sql .= " from rhlotacalend ";
     $sql .= "      inner join rhcadcalend  on  rhcadcalend.rh53_calend = rhlotacalend.rh64_calend";
     $sql2 = "";
     if($dbwhere==""){
       if($rh64_lota!=null ){
         $sql2 .= " where rhlotacalend.rh64_lota = $rh64_lota "; 
       } 
     }else if($dbwhere != ""){
       $sql2 = " where $dbwhere";
     }
     $sql .= $sql2;
