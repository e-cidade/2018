       $this->erro_status = "0";
       $this->numrows_excluir = 0;
       return false;
     }else{
       if(pg_affected_rows($result)==0){
         $this->erro_banco = "";
         $this->erro_sql = "Motivos de baixa de bens nao Encontrado. Exclusão não Efetuada.\\n";
