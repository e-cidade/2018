   //funcao construtor da classe 
   function cl_bensmotbaixa() { 
     //classes dos rotulos dos campos
     $this->rotulo = new rotulo("bensmotbaixa"); 
     $this->pagina_retorno =  basename($GLOBALS["HTTP_SERVER_VARS"]["PHP_SELF"]);
   }
   //funcao erro 
   function erro($mostra,$retorna) { 
     if(($this->erro_status == "0") || ($mostra == true && $this->erro_status != null )){
        echo "<script>alert(\"".$this->erro_msg."\");</script>";
        if($retorna==true){
           echo "<script>location.href='".$this->pagina_retorno."'</script>";
        }
     }
   }
   // funcao para atualizar campos
   function atualizacampos($exclusao=false) {
     if($exclusao==false){
       $this->t51_motivo = ($this->t51_motivo == ""?@$GLOBALS["HTTP_POST_VARS"]["t51_motivo"]:$this->t51_motivo);
       $this->t51_descr = ($this->t51_descr == ""?@$GLOBALS["HTTP_POST_VARS"]["t51_descr"]:$this->t51_descr);
     }else{
       $this->t51_motivo = ($this->t51_motivo == ""?@$GLOBALS["HTTP_POST_VARS"]["t51_motivo"]:$this->t51_motivo);
     }
   }
   // funcao para inclusao
   function incluir ($t51_motivo){ 
      $this->atualizacampos();
     if($this->t51_descr == null ){ 
       $this->erro_sql = " Campo Descrição do motivo da baixa nao Informado.";
       $this->erro_campo = "t51_descr";
       $this->erro_banco = "";
       $this->erro_msg   = "Usuário: \\n\\n ".$this->erro_sql." \\n\\n";
       $this->erro_msg   .=  str_replace('"',"",str_replace("'","",  "Administrador: \\n\\n ".$this->erro_banco." \\n"));
       $this->erro_status = "0";
       return false;
     }
     if($t51_motivo == "" || $t51_motivo == null ){
       $result = db_query("select nextval('bensmotbaixa_t51_motivo_seq')"); 
       if($result==false){
         $this->erro_banco = str_replace("\n","",@pg_last_error());
         $this->erro_sql   = "Verifique o cadastro da sequencia: bensmotbaixa_t51_motivo_seq do campo: t51_motivo"; 
         $this->erro_msg   = "Usuário: \\n\\n ".$this->erro_sql." \\n\\n";
         $this->erro_msg   .=  str_replace('"',"",str_replace("'","",  "Administrador: \\n\\n ".$this->erro_banco." \\n"));
         $this->erro_status = "0";
         return false; 
       }
       $this->t51_motivo = pg_result($result,0,0); 
     }else{
       $result = db_query("select last_value from bensmotbaixa_t51_motivo_seq");
       if(($result != false) && (pg_result($result,0,0) < $t51_motivo)){
         $this->erro_sql = " Campo t51_motivo maior que último número da sequencia.";
         $this->erro_banco = "Sequencia menor que este número.";
         $this->erro_msg   = "Usuário: \\n\\n ".$this->erro_sql." \\n\\n";
         $this->erro_msg   .=  str_replace('"',"",str_replace("'","",  "Administrador: \\n\\n ".$this->erro_banco." \\n"));
         $this->erro_status = "0";
         return false;
       }else{
         $this->t51_motivo = $t51_motivo; 
       }
     }
     if(($this->t51_motivo == null) || ($this->t51_motivo == "") ){ 
       $this->erro_sql = " Campo t51_motivo nao declarado.";
       $this->erro_banco = "Chave Primaria zerada.";
       $this->erro_msg   = "Usuário: \\n\\n ".$this->erro_sql." \\n\\n";
       $this->erro_msg   .=  str_replace('"',"",str_replace("'","",  "Administrador: \\n\\n ".$this->erro_banco." \\n"));
       $this->erro_status = "0";
       return false;
     }
     $sql = "insert into bensmotbaixa(
                                       t51_motivo 
                                      ,t51_descr 
                       )
                values (
                                $this->t51_motivo 
                               ,'$this->t51_descr' 
                      )";
     $result = db_query($sql); 
     if($result==false){ 
       $this->erro_banco = str_replace("\n","",@pg_last_error());
       if( strpos(strtolower($this->erro_banco),"duplicate key") != 0 ){
         $this->erro_sql   = "Motivos de baixa de bens ($this->t51_motivo) nao Incluído. Inclusao Abortada.";
         $this->erro_msg   = "Usuário: \\n\\n ".$this->erro_sql." \\n\\n";
         $this->erro_banco = "Motivos de baixa de bens já Cadastrado";
         $this->erro_msg   .=  str_replace('"',"",str_replace("'","",  "Administrador: \\n\\n ".$this->erro_banco." \\n"));
       }else{
         $this->erro_sql   = "Motivos de baixa de bens ($this->t51_motivo) nao Incluído. Inclusao Abortada.";
         $this->erro_msg   = "Usuário: \\n\\n ".$this->erro_sql." \\n\\n";
         $this->erro_msg   .=  str_replace('"',"",str_replace("'","",  "Administrador: \\n\\n ".$this->erro_banco." \\n"));
       }
       $this->erro_status = "0";
       $this->numrows_incluir= 0;
       return false;
     }
     $this->erro_banco = "";
     $this->erro_sql = "Inclusao efetuada com Sucesso\\n";
         $this->erro_sql .= "Valores : ".$this->t51_motivo;
     $this->erro_msg   = "Usuário: \\n\\n ".$this->erro_sql." \\n\\n";
     $this->erro_msg   .=  str_replace('"',"",str_replace("'","",  "Administrador: \\n\\n ".$this->erro_banco." \\n"));
     $this->erro_status = "1";
     $this->numrows_incluir= pg_affected_rows($result);
     $resaco = $this->sql_record($this->sql_query_file($this->t51_motivo));
     if(($resaco!=false)||($this->numrows!=0)){
       $resac = db_query("select nextval('db_acount_id_acount_seq') as acount");
       $acount = pg_result($resac,0,0);
       $resac = db_query("insert into db_acountacesso values($acount,".db_getsession("DB_acessado").")");
       $resac = db_query("insert into db_acountkey values($acount,5762,'$this->t51_motivo','I')");
       $resac = db_query("insert into db_acount values($acount,912,5762,'','".AddSlashes(pg_result($resaco,0,'t51_motivo'))."',".db_getsession('DB_datausu').",".db_getsession('DB_id_usuario').")");
       $resac = db_query("insert into db_acount values($acount,912,5763,'','".AddSlashes(pg_result($resaco,0,'t51_descr'))."',".db_getsession('DB_datausu').",".db_getsession('DB_id_usuario').")");
     }
     return true;
   } 
