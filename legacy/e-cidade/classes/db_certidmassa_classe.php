   function sql_query ( $v13_certid=null,$campos="*",$ordem=null,$dbwhere=""){ 
     $sql = "select ";
     if($campos != "*" ){
       $campos_sql = split("#",$campos);
       $virgula = "";
       for($i=0;$i<sizeof($campos_sql);$i++){
         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }else{
       $sql .= $campos;
     }
     $sql .= " from certidmassa ";
     $sql .= "      inner join certid  on  certid.v13_certid = certidmassa.v13_certid";
     $sql .= "      inner join db_config  on  db_config.codigo = certid.v13_instit";
     $sql .= "      inner join db_usuarios  on  db_usuarios.id_usuario = certid.v13_login";
     $sql2 = "";
     if($dbwhere==""){
       if($v13_certid!=null ){
         $sql2 .= " where certidmassa.v13_certid = $v13_certid "; 
       } 
     }else if($dbwhere != ""){
       $sql2 = " where $dbwhere";
     }
     $sql .= $sql2;
     if($ordem != null ){
       $sql .= " order by ";
       $campos_sql = split("#",$ordem);
       $virgula = "";
       for($i=0;$i<sizeof($campos_sql);$i++){
         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }
     return $sql;
  }
