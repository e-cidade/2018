        }
     }
   }
   // funcao para atualizar campos
   function atualizacampos($exclusao=false) {
     if($exclusao==false){
       $this->db05_idtipo = ($this->db05_idtipo == ""?@$GLOBALS["HTTP_POST_VARS"]["db05_idtipo"]:$this->db05_idtipo);
