   // funcao para atualizar campos
   function atualizacampos($exclusao=false) {
     if($exclusao==false){
       $this->at04_codtipo = ($this->at04_codtipo == ""?@$GLOBALS["HTTP_POST_VARS"]["at04_codtipo"]:$this->at04_codtipo);
