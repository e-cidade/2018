   function sql_query ( $j65_lograd=null,$j65_ceplog=null,$campos="*",$ordem=null,$dbwhere=""){ 
     $sql = "select ";
     if($campos != "*" ){
       $campos_sql = split("#",$campos);
       $virgula = "";
       for($i=0;$i<sizeof($campos_sql);$i++){
         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }else{
       $sql .= $campos;
     }
     $sql .= " from logradcep ";
     $sql .= "      inner join ruas  on  ruas.j14_codigo = logradcep.j65_lograd";
     $sql .= "      inner join db_ceplog  on  db_ceplog.db11_codlog = logradcep.j65_ceplog";
     $sql .= "      inner join db_cepmunic  on  db_cepmunic.db10_codigo = db_ceplog.db11_codigo";
     $sql2 = "";
     if($dbwhere==""){
       if($j65_lograd!=null ){
         $sql2 .= " where logradcep.j65_lograd = $j65_lograd "; 
       } 
       if($j65_ceplog!=null ){
         if($sql2!=""){
            $sql2 .= " and ";
         }else{
            $sql2 .= " where ";
         } 
         $sql2 .= " logradcep.j65_ceplog = $j65_ceplog "; 
       } 
     }else if($dbwhere != ""){
       $sql2 = " where $dbwhere";
     }
     $sql .= $sql2;
     if($ordem != null ){
       $sql .= " order by ";
       $campos_sql = split("#",$ordem);
       $virgula = "";
       for($i=0;$i<sizeof($campos_sql);$i++){
         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }
     return $sql;
  }
