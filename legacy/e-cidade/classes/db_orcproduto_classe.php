   // funcao para inclusao
   function incluir ($o22_codproduto){ 
      $this->atualizacampos();
     if($this->o22_descrprod == null ){ 
       $this->erro_sql = " Campo Descrição nao Informado.";
       $this->erro_campo = "o22_descrprod";
       $this->erro_banco = "";
       $this->erro_msg   = "Usuário: \\n\\n ".$this->erro_sql." \\n\\n";
       $this->erro_msg   .=  str_replace('"',"",str_replace("'","",  "Administrador: \\n\\n ".$this->erro_banco." \\n"));
       $this->erro_status = "0";
       return false;
     }
     if($o22_codproduto == "" || $o22_codproduto == null ){
       $result = db_query("select nextval('orcproduto_o22_codproduto_seq')"); 
       if($result==false){
         $this->erro_banco = str_replace("\n","",@pg_last_error());
