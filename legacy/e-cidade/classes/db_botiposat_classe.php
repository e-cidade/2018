   // funcao para inclusao
   function incluir ($bo02_codsat){ 
      $this->atualizacampos();
     if($this->bo02_descsat == null ){ 
       $this->erro_sql = " Campo Satisfação nao Informado.";
       $this->erro_campo = "bo02_descsat";
       $this->erro_banco = "";
       $this->erro_msg   = "Usuário: \\n\\n ".$this->erro_sql." \\n\\n";
       $this->erro_msg   .=  str_replace('"',"",str_replace("'","",  "Administrador: \\n\\n ".$this->erro_banco." \\n"));
       $this->erro_status = "0";
       return false;
     }
     if($bo02_codsat == "" || $bo02_codsat == null ){
       $result = db_query("select nextval('tel_codsat_seq')"); 
       if($result==false){
         $this->erro_banco = str_replace("\n","",@pg_last_error());
