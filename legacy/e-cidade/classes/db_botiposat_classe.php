   // funcao para atualizar campos
   function atualizacampos($exclusao=false) {
     if($exclusao==false){
       $this->bo02_codsat = ($this->bo02_codsat == ""?@$GLOBALS["HTTP_POST_VARS"]["bo02_codsat"]:$this->bo02_codsat);
       $this->bo02_descsat = ($this->bo02_descsat == ""?@$GLOBALS["HTTP_POST_VARS"]["bo02_descsat"]:$this->bo02_descsat);
