       $this->erro_status = "0";
       return false;
     }
     $sql = "insert into conrelparametro(
                                       c18_parametro 
                                      ,c18_valor 
                       )
                values (
                                '$this->c18_parametro' 
                               ,'$this->c18_valor' 
                      )";
     $result = db_query($sql); 
     if($result==false){ 
       $this->erro_banco = str_replace("\n","",@pg_last_error());
       if( strpos(strtolower($this->erro_banco),"duplicate key") != 0 ){
         $this->erro_sql   = "c18 ($this->c18_parametro) nao Incluído. Inclusao Abortada.";
