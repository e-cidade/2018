<H2 CLASS="western"><A NAME="4.5.Cancelar Impress&atilde;o da Etiqueta|outline"></A>
<A HREF="#Sum&aacute;rio1|region" NAME="db6924db">Cancelar Impress&atilde;o
da Etiqueta</A></H2>
<P CLASS="western">	Ap&oacute;s processar alguma etiqueta para
impress&atilde;o, o sistema considera esta como j&aacute; impressa
redirecionando a mesma para outra tabela, n&atilde;o ficando mais
junto das demais ainda n&atilde;o processadas, viabilizando o
sistema, diferenciando-as e evitando que uma etiqueta j&aacute;
impressa seja impressa novamente. Caso a etiqueta seja apenas
processada e n&atilde;o impressa, o sistema permitir&aacute; que esta
retorne para junto das demais, sendo estas cancelada a impress&atilde;o,
conforme mostra a figura cancelar impress&atilde;o, o sistema
disponibiliza um link de pesquisa, clicando nele o sistema abrir&aacute;
a lookup com todas as rela&ccedil;&otilde;es de etiquetas j&aacute;
processadas e alguns filtros de busca r&aacute;pida (figura lookup
etiqueta impressa), busque e selecione a impress&atilde;o desejada, o
sistema retornar&aacute; para a tela inicial e carregar&aacute; a
rela&ccedil;&atilde;o dos exemplares que foram processadas as
etiquetas, marque quais delas deseja cancelar a impress&atilde;o e
clique no bot&atilde;o cancelar impress&atilde;o, conforme mostra a
figura cancelar impress&atilde;o 2. Confirme o cancelamento das
impress&otilde;es, ap&oacute;s o sistema retornar&aacute; para a
figura cancelar impress&atilde;o.</P>
