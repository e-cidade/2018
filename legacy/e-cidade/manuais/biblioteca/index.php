<H1 CLASS="western"><A NAME="1.Cadastros|outline"></A>Cadastros</H1>
<P CLASS="western"><FONT COLOR="#ff0000"><FONT FACE="Helvetica-BoldOblique, sans-serif"><FONT SIZE=4><I><U>Aten&ccedil;&atilde;o</U></I></FONT></FONT></FONT><FONT COLOR="#ff0000"><FONT FACE="Helvetica-BoldOblique, sans-serif"><FONT SIZE=3 STYLE="font-size: 12pt">:</FONT></FONT></FONT></P>
<P CLASS="western">	<I>Nas rotinas as seguir, o usu&aacute;rio dever&aacute;
ter aten&ccedil;&atilde;o para n&atilde;o repetir um cadastro j&aacute;
efetuado. Se a biblioteca possuir mais do que um acervo de um
determinado autor ou editora, uma vez cadastrado no sistema, basta
pesquisar seu nome e inclui-lo no cadastro. O mesmo deve-se para as
demais rotinas, com exce&ccedil;&atilde;o da rotina ACERVO, pois se a
biblioteca possuir dois exemplares ou mais de um acervo, ao
cadastra-lo, os demais exemplares dever&atilde;o ser registrados na
quarta aba EXEMPLARES, repetindo seu registro e gerando o c&oacute;digo
de barras para cada exemplar do acervo.</I></P>
