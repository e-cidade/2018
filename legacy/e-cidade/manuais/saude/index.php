<H2 CLASS="western"><A NAME="4.6.Fechamento de Compet&ecirc;ncia|outline"></A>
<STRONG><A CLASS="western" HREF="#Sum&aacute;rio1|region" NAME="db6971db"><I><B>Fechamento
de Compet&ecirc;ncia</B></I></A></STRONG></H2>
<P STYLE="text-indent: 2cm; margin-bottom: 0cm"><STRONG><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Esta
rotina &eacute; necess&aacute;ria para que determinados relat&oacute;rios
, arquivos - exemplo o BPA &ndash; , que s&atilde;o impostos pela
legisla&ccedil;&atilde;o , possam ser gerados por compet&ecirc;ncia ,
ou seja , informa&ccedil;&otilde;es que foram geradas em um
determinado per&iacute;odo. Para atender a esta imposi&ccedil;&atilde;o
&eacute; que necessitamos desta rotina de fechamento. Por ela podemos
saber a qual compet&ecirc;ncia a informa&ccedil;&atilde;o pertence. O
faturamento total do sistema gerado pela a secretaria da sa&uacute;de
(o BPA) &eacute; por compet&ecirc;ncia. </SPAN></SPAN></STRONG>
</P>
<P STYLE="text-indent: 2cm; margin-bottom: 0cm"><BR>
</P>
<P STYLE="text-indent: 2cm; margin-bottom: 0cm"><STRONG><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Aten&ccedil;&atilde;o
: </SPAN></SPAN></STRONG>
</P>
<P STYLE="text-indent: 2cm; margin-bottom: 0cm"><STRONG>     <SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">1
- Uma Compet&ecirc;ncia conter&aacute; todas as FAAs que est&atilde;o
com o status Digitadas 'Sim' e foram digitada dentro do per&iacute;odo
indicado nos campos Per&iacute;odo de Compet&ecirc;ncia  e mais as
FAAs antigas que n&atilde;o entraram em outras compet&ecirc;ncias.</SPAN></SPAN></STRONG></P>
<P STYLE="text-indent: 2cm; margin-bottom: 0cm"><STRONG>    <SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">2
- O sistema n&atilde;o abrir&aacute; uma compet&ecirc;ncia enquanto
estiver outra compet&ecirc;ncia superior com o status fechado.  </SPAN></SPAN></STRONG>
</P>
<P STYLE="text-indent: 2cm; margin-bottom: 0cm"><STRONG>    <SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">3
- Quando fechar uma compet&ecirc;ncia o sistema abre uma nova
automaticamente para registrar a pr&oacute;xima compet&ecirc;ncia.</SPAN></SPAN></STRONG></P>
<P STYLE="text-indent: 2cm; margin-bottom: 0cm"><BR>
</P>
<P STYLE="text-indent: 2cm; margin-bottom: 0cm"><BR>
</P>
<P STYLE="text-indent: 2cm; margin-bottom: 0cm"><BR>
</P>
<P STYLE="margin-bottom: 0cm"><STRONG><SPAN STYLE="font-style: normal">Inclus&atilde;o</SPAN></STRONG></P>
<P STYLE="text-indent: 2cm; margin-bottom: 0cm"><STRONG><I>Compet&ecirc;ncia:
</I></STRONG><STRONG><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Preencher
o m&ecirc;s e ano da compet&ecirc;ncia que se quer fechar.</SPAN></SPAN></STRONG></P>
<P STYLE="text-indent: 2cm; margin-bottom: 0cm"><STRONG><I>Per&iacute;odo
de Fechamento: </I></STRONG><STRONG><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Colocar
a data inicial e final da compet&ecirc;ncia que est&aacute; sendo
fechada. Mas a data Inicial deve ser maior que a data final da ultima
compet&ecirc;ncia.</SPAN></SPAN></STRONG></P>
<P STYLE="text-indent: 2cm; margin-bottom: 0cm"><STRONG><I>Data:
</I></STRONG><STRONG><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Busca
como padr&atilde;o a data do sistema do dia do fechamento.</SPAN></SPAN></STRONG></P>
<P STYLE="text-indent: 2cm; margin-bottom: 0cm"><STRONG><I>Descri&ccedil;&atilde;o:
 </I></STRONG><STRONG><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Preencher
a descri&ccedil;&atilde;o sobre o fechamento de compet&ecirc;ncia.  </SPAN></SPAN></STRONG>
</P>
<P STYLE="text-indent: 2cm; margin-bottom: 0cm"><STRONG><I>Tipo: </I></STRONG><STRONG><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Este
campo e o status do fechamento, que informa se a compet&ecirc;ncia
esta  (Fechada ou Aberta). </SPAN></SPAN></STRONG>
</P>
