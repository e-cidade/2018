<H1 CLASS="western"><A NAME="1.Cadastros|outline"></A>Cadastros</H1>
<P STYLE="text-indent: 2cm; margin-bottom: 0cm">Nesta op&ccedil;&atilde;o
tem-se acesso a todos os cadastros do Modulo Sa&uacute;de, como o
cadastro dos Pacientes (CGS) , os Profissionais (M&eacute;dicos,
Enfermeiras), as UPS &ndash; Unidades Postos de Sa&uacute;de, as
Tabelas Nacionais (CNES), fundamentais para valida&ccedil;&atilde;o
das informa&ccedil;&otilde;es (Procedimentos , CBO, CID , etc ...). &Egrave;
importante deixa-los sempre bem atualizados.</P>
<P STYLE="text-indent: 2cm; margin-bottom: 0cm"><BR>
</P>
<H2 CLASS="western"><A NAME="1.1.CGS|outline"></A><A CLASS="western" HREF="#Sum&aacute;rio1|region" NAME="db1045399db">CGS</A></H2>
<P STYLE="text-indent: 2cm; margin-bottom: 0cm">Este cadastro
consiste em criar uma base de dados dos pacientes de todo o
munic&iacute;pio, permitindo interligar todas as unidades no mesmo
Cadastro Geral da Sa&uacute;de. O CGS &eacute; o cadastro mais
importante desse m&oacute;dulo e da &aacute;rea do sistema de sa&uacute;de,
pois todos os dados converte para o hist&oacute;rico de dados do
paciente. Para fazer inclus&otilde;es nesse cadastro, preencha os
seguinte campos.</P>
<P ALIGN=JUSTIFY><BR><BR>
</P>
<H3 CLASS="western"><A NAME="1.1.1.Inclus&atilde;o|outline"></A><A CLASS="western" HREF="#Sum&aacute;rio1|region" NAME="db1045410db">Inclus&atilde;o</A></H3>
<P ALIGN=JUSTIFY><B>ABA Dados Pessoais</B></P>
<P STYLE="text-indent: 2cm; margin-bottom: 0cm"><I><B>CGS: </B></I>O
campo CGS &ndash; Cadastro Geral da Sa&uacute;de ser&aacute; gerado
automaticamente e corresponde ao c&oacute;digo do item que est&aacute;
sendo inclu&iacute;do.</P>
<P STYLE="text-indent: 2cm; margin-bottom: 0cm"><I><B>CGS do
Munic&iacute;pio: </B></I>O campo CGS do Munic&iacute;pio indica se o
Paciente reside ou n&atilde;o no munic&iacute;pio, se n&atilde;o
desabilita as ancoras CEP , ENDERE&Ccedil;O e BAIRRO.</P>
<P STYLE="text-indent: 2cm; margin-bottom: 0cm"><I><B>CPF: </B></I>O
campo CPF &ndash; C&oacute;digo Pessoal F&iacute;sica n&atilde;o &eacute;
obrigat&oacute;rio, mas serve como campo de pesquisa para o CGS,
ent&atilde;o &eacute; importante ser preenchido. Este campo tamb&eacute;m
serve com chave no caso de cadastro duplicado de CGS, n&atilde;o
permite cadastrar dois com o mesmo CPF.</P>
<P STYLE="text-indent: 2cm; margin-bottom: 0cm"><I><B>Nome: </B></I>&Eacute;
cadastrado o nome do paciente, permitindo sua identifica&ccedil;&atilde;o.
Este campo &eacute; obrigat&oacute;rio.</P>
<P STYLE="text-indent: 2cm; margin-bottom: 0cm"><I><B>Pai : </B></I>Neste
campo deve ser cadastrado o nome do pai do paciente, n&atilde;o sendo
uma chave para inclus&atilde;o do CGS e n&atilde;o &eacute;
obrigat&oacute;rio.</P>
<P STYLE="text-indent: 2cm; margin-bottom: 0cm"><I><B>M&atilde;e:</B></I>
<SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Neste
campo deve ser cadastrado o nome do m&atilde;e do paciente, sendo
usada como uma chave para inclus&atilde;o do CGS junto com a data de
nascimento, ent&atilde;o este &eacute; um campo obrigat&oacute;rio no
sistema.</SPAN></SPAN></P>
<P STYLE="text-indent: 2cm; margin-bottom: 0cm"><I><B>Data
Nascimento:</B></I> <SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Nesse
campo &eacute; cadastrado a data de nascimento do CGS, que &eacute;
campo obrigat&oacute;rio no sistema e chave conjunta com o nome da
m&atilde;e para o CGS. Ent&atilde;o o sistema n&atilde;o permite
cadastrar um CGS que possui o mesmo nome, nome da m&atilde;e e data
de nascimento.</SPAN></SPAN></P>
<P STYLE="text-indent: 2cm; margin-bottom: 0cm"><I><B>Sexo:</B></I>
<SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Selecionar
neste campo o sexo do paciente, que foi buscado os tipos do governo
do minist&eacute;rio da sa&uacute;de.</SPAN></SPAN></P>
<P STYLE="text-indent: 2cm; margin-bottom: 0cm"><I><B>Estado Civil:
</B></I>Selecionar neste campo o estado civil do paciente, que foi
buscado os tipos do governo do minist&eacute;rio da sa&uacute;de.</P>
<P STYLE="text-indent: 2cm; margin-bottom: 0cm"><I><B>Cart&atilde;o
