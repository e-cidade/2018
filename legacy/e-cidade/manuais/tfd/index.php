<P CLASS="western"><BR><BR>
</P>
<H3 CLASS="western"><A NAME="__RefHeading__2123_759021697"></A>Inclus&atilde;o</H3>
<P CLASS="western">	O objetivo dessa rotina &eacute; definir e dar
manuten&ccedil;&atilde;o a tabela ajuda de custo. O valor &eacute; o
de ressarcimento para o procedimento, pelo governo. Essa tabela ser&aacute;
usada se deduzir o valor a ser ressarcido pelo governo pelos gastos
incorridos pela prefeitura na ajuda de custo do paciente e seu
acompanhante. Esse cadastro pela sua pr&oacute;pria natureza
origina-se das tabelas nacionais j&aacute; que os valores vem de l&aacute;.
O controle ser&aacute; feito dentro do per&iacute;odo informado.</P>
<P CLASS="western"><BR><BR>
</P>
<P CLASS="western"><BR><BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY>Campos</P>
<P CLASS="western" ALIGN=JUSTIFY>C&oacute;digo: Sequencial gerado
automaticamente pelo sistema</P>
<P CLASS="western" ALIGN=JUSTIFY>Procedimento: Campo link para
sele&ccedil;&atilde;o e inser&ccedil;&atilde;o dos procedimentos a
ser utilizados na ajuda de custo.</P>
<P CLASS="western" ALIGN=JUSTIFY>Autom&aacute;tico: Para sele&ccedil;&atilde;o
do tipo lan&ccedil;amento (se selecionado &ldquo;manual&rdquo; poder&aacute;
ser digitado o valor e &ldquo;autom&aacute;tico&rdquo; ser&aacute;
informado pelo sistema de acordo com as tabelas nacionais).</P>
<P CLASS="western" ALIGN=JUSTIFY>Per&iacute;odo</P>
<P CLASS="western" ALIGN=JUSTIFY>In&iacute;cio: Informar data de
in&iacute;cio do per&iacute;odo para controle da ajuda de custo.</P>
<P CLASS="western" ALIGN=JUSTIFY>Fim: Informar data de fim do per&iacute;odo
para controle da ajuda de custo.</P>
