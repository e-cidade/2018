gerado automaticamente pelo sistema.</P>
<P CLASS="western"><B>Descri&ccedil;&atilde;o</B>: Cabe nesse campo
fazer a Descri&ccedil;&atilde;o do documento.</P>
<P CLASS="western"><B>Validade:</B> data at&eacute; quando vai ser
valido. Se nulo vale infinitamente.</P>
