<P CLASS="western" STYLE="margin-bottom: 0cm"><I><SPAN STYLE="text-decoration: none"><B>	Concedente:</B></SPAN></I>
Quem est&aacute; concedendo o Programa;</P>
<P CLASS="western" STYLE="margin-bottom: 0cm"><I><SPAN STYLE="text-decoration: none"><B>	Controle
de Participa&ccedil;&atilde;o: </B></SPAN></I>Controlar a
participa&ccedil;&atilde;o de pessoas no Programa|:</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm">		<I><B>Sem
Aviso e Sem Bloqueio:</B></I> Selecionando esta op&ccedil;&atilde;o,
o sistema n&atilde;o vai avisar nem bloquear a participa&ccedil;&atilde;o
da mesma pessoa, mais de uma vez no programa;</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm">		<I><B>Com
Aviso e Sem Bloqueio:</B></I> Selecionando esta op&ccedil;&atilde;o,
o sistema vai avisar caso a pessoa j&aacute; esteja participando do
Programa, mas n&atilde;o vai bloquear caso o usu&aacute;rio queira
cadastr&aacute;-la novamente;</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm">		<I><B>Com
Aviso e Bloqueio:</B></I> Selecionando esta op&ccedil;&atilde;o, o
sistema vai avisar caso a pessoa j&aacute; esteja participando do
Programa e n&atilde;o vai permitir um novo cadastro desta pessoa
neste Programa.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><I><SPAN STYLE="text-decoration: none"><B>	Controle
de Quantidade:</B></SPAN></I> Controla a quantidade de pessoas que
ser&atilde;o contempladas com este Programa:</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm">		<I><B>Sem
Limite:</B></I> O sistema permite cadastrar pessoas sem limite para o
Programa;</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm">		<I><B>M&aacute;xima
Determinada:</B></I> O sistema permite cadastrar um determinado
n&uacute;mero de pessoas para serem beneficiadas pelo Programa;</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><I><SPAN STYLE="text-decoration: none"><B>	Quantidade
de Beneficiados:</B></SPAN></I> Descrever o n&uacute;mero m&aacute;ximo
de pessoas que ser&atilde;o beneficiadas com o Programa. Este campo
s&oacute; abrir&aacute;, se no campo Controle de Quantidade for
selecionada a op&ccedil;&atilde;o M&aacute;xima Determinada;</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><I><SPAN STYLE="text-decoration: none"><B>	Per&iacute;odo
Validade:</B></SPAN></I><SPAN STYLE="text-decoration: none">
</SPAN>Descrever o Per&iacute;odo de validade do Programa;</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><I><SPAN STYLE="text-decoration: none"><B>	Descri&ccedil;&atilde;o
do Contrato:</B></SPAN></I><SPAN STYLE="text-decoration: none"> </SPAN><FONT COLOR="#000000"><SPAN STYLE="text-decoration: none">Fazer
