cedidos pelo Programa;</SPAN></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><I><SPAN STYLE="text-decoration: none"><B>	Dia
Padr&atilde;o para Pagamento: </B></SPAN></I><SPAN STYLE="text-decoration: none">Informar
o dia padr&atilde;o para o pagamento das parcelas. (Vir&aacute;
automaticamente dos par&acirc;metros);</SPAN></P>
