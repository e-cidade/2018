Padr&atilde;o para o Pagamento:</B></SPAN></I><SPAN STYLE="text-decoration: none">
Selecionar o c&oacute;digo da Receita que ir&aacute; ser usada para o
pagamento.  (Vir&aacute; automaticamente dos par&acirc;metros);</SPAN></P>
