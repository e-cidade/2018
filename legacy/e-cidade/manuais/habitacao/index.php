<P CLASS="western" STYLE="margin-bottom: 0cm"><I><SPAN STYLE="text-decoration: none"><B>	Sequencial:</B></SPAN></I>
Campo vai gerar automaticamente o c&oacute;digo do Programa;</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><I><SPAN STYLE="text-decoration: none"><B>	Grupo
de Programa:</B></SPAN></I> Selecionar um dos grupos que se encaixe
no programa que deseja cadastrar;</P>
<P CLASS="western" STYLE="margin-bottom: 0cm"><I><SPAN STYLE="text-decoration: none"><B>	Descri&ccedil;&atilde;o:
</B></SPAN></I>Descrever o Nome do Programa;</P>
