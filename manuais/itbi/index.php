<H2 CLASS="western"><A NAME="__RefHeading__12181_1553361321"></A><FONT FACE="Times, serif"><FONT SIZE=3><A HREF="#Sum&aacute;rio1_Head|region" NAME="db2563db">Tipo
de Situa&ccedil;&atilde;o</A></FONT></FONT></H2>
<P CLASS="western" ALIGN=JUSTIFY><FONT FACE="Times, serif"><FONT SIZE=3>	Esse
cadastro consiste em criar uma base contendo os tipos de situa&ccedil;&otilde;es
dos im&oacute;veis em rela&ccedil;&atilde;o a quadra onde est&atilde;o
localizados.</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><FONT FACE="Times, serif"><FONT SIZE=3><B>	Ex.:</B>	Interno
&ndash; se o im&oacute;vel fica no meio da quadra.</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><FONT FACE="Times, serif"><FONT SIZE=3>		Encravado
&ndash; se o im&oacute;vel n&atilde;o faz testada com nenhum
logradouro.</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><FONT FACE="Times, serif"><FONT SIZE=3>		Esquina
&ndash; se o im&oacute;vel est&aacute; situado em uma esquina.</FONT></FONT></P>
<H3 CLASS="western" STYLE="font-weight: normal"><A NAME="__RefHeading__12107_1553361321"></A>
<FONT FACE="Times, serif"><FONT SIZE=3><A HREF="#Sum&aacute;rio1_Head|region" NAME="db2564db">Inclus&atilde;o</A></FONT></FONT></H3>
<P CLASS="western" ALIGN=JUSTIFY><FONT FACE="Times, serif"><FONT SIZE=3>	Para
fazer inclus&otilde;es nesse cadastro, preencha os seguinte campos:</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="font-weight: normal"><FONT FACE="Times, serif"><FONT SIZE=3><I>C&oacute;digo:
</I>Esse campo ser&aacute; gerado automaticamente e corresponde ao
c&oacute;digo do item que est&aacute; sendo inclu&iacute;do.</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><FONT FACE="Times, serif"><FONT SIZE=3><I><SPAN STYLE="font-weight: normal">Descri&ccedil;&atilde;o:</SPAN></I><I><B>
</B></I>Digite a descri&ccedil;&atilde;o (nome) da situa&ccedil;&atilde;o
que est&aacute; incluindo.</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><FONT FACE="Times, serif"><FONT SIZE=3>	Ap&oacute;s
dever&aacute; clicar no bot&atilde;o Incluir.</FONT></FONT></P>
