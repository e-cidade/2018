<H1 CLASS="western"><A NAME="__RefHeading__12129_1553361321"></A><FONT FACE="Times, serif"><FONT SIZE=3><A HREF="#Sum&aacute;rio1_Head|region" NAME="db1822db">Consultas</A></FONT></FONT></H1>
<H2 CLASS="western"><A NAME="__RefHeading__12131_1553361321"></A><FONT FACE="Times, serif"><FONT SIZE=3><A HREF="#Sum&aacute;rio1_Head|region" NAME="db4232db">ITBI</A></FONT></FONT></H2>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0.2cm; font-style: normal; font-weight: normal">
<FONT FACE="Times, serif"><FONT SIZE=3>	Para efetuar a consulta de
ITBI, deve ser informado pelo menos um dos filtros, conforme
demonstrado nas figuras 09 e 09A.</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><FONT FACE="Times, serif"><FONT SIZE=3><I><SPAN STYLE="font-weight: normal">Guia:
</SPAN></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Informando
o n&uacute;mero da guia de ITBI que foi liberada.</SPAN></SPAN></FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0.2cm"><FONT FACE="Times, serif"><FONT SIZE=3><I><SPAN STYLE="font-weight: normal">Adquirente</SPAN>:
</I><SPAN STYLE="font-style: normal">Informando o nome do adquirente
ser&aacute; apresentada uma lista contendo todos os cadastros de ITBI
que foram efetuados nesse nome.</SPAN></FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0.2cm"><FONT FACE="Times, serif"><FONT SIZE=3><I><SPAN STYLE="font-weight: normal">Transmitente</SPAN>:<B>
</B></I><SPAN STYLE="font-style: normal">Informando o nome do
transmitente ser&aacute; apresentada uma lista contendo todos os
</SPAN><SPAN STYLE="font-style: normal">cadastros de ITBI que foram
efetuados nesse nome.</SPAN></FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0.2cm"><FONT FACE="Times, serif"><FONT SIZE=3><I><SPAN STYLE="font-weight: normal">Data
inicial / Data final</SPAN>: </I><SPAN STYLE="font-style: normal">Informando
o per&iacute;odo desejado ser&aacute; apresentada uma lista contendo
todos os cadastros de ITBI efetuados no intervalo de datas informado.</SPAN></FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0.2cm; font-style: normal">
<FONT FACE="Times, serif"><FONT SIZE=3>	Caso seja informada apenas a
data inicial, ser&aacute; apresentada uma lista com os cadastros
efetuados a partir dessa data at&eacute; a data atual.</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0.2cm; font-style: normal">
<FONT FACE="Times, serif"><FONT SIZE=3>	Caso seja informada apenas a
data final, ser&aacute; apresentada uma lista com todos os cadastros
de ITBI efetuados at&eacute; a data indicada, desconsiderando a data
inicial.</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0.2cm"><FONT FACE="Times, serif"><FONT SIZE=3><I><SPAN STYLE="font-weight: normal">Tipo
de ITBI</SPAN>: </I><SPAN STYLE="font-style: normal">Selecione o tipo
de ITBI que deseja apresentar na consulta conforme os filtros
mencionados anteriormente.</SPAN></FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0.2cm; font-style: normal">
<FONT FACE="Times, serif"><FONT SIZE=3><B>	Ex.:</B> caso informe a
data inicial: 01/01/2007 e data final: 31/12/2007 e selecione o tipo
de ITBI urbana, ser&aacute; apresentada uma lista somente com as
ITBIs urbanas cadastradas nesse intervalo de datas.</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0.2cm"><FONT FACE="Times, serif"><FONT SIZE=3><I><SPAN STYLE="font-weight: normal">Matr&iacute;cula
do im&oacute;vel</SPAN>:<B> </B></I><SPAN STYLE="font-style: normal">Informe
o n&uacute;mero de uma determinada matr&iacute;cula que deseja
consultar a ITBI.</SPAN></FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0.2cm; font-style: normal">
<FONT FACE="Times, serif"><FONT SIZE=3>	Caso tenha sido inclu&iacute;da
mais de uma ITBI para a matr&iacute;cula indicada, ser&atilde;o
apresentados os registros para selecionar o que deseja consultar. </FONT></FONT>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0.2cm"><FONT FACE="Times, serif"><FONT SIZE=3><I><SPAN STYLE="font-weight: normal">Setor:</SPAN><B>
</B></I><SPAN STYLE="font-style: normal">Informe o setor desejado e
ser&aacute; apresentada uma lista com todas as ITBIs cadastradas para
os im&oacute;veis desse setor.</SPAN></FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0.2cm"><FONT FACE="Times, serif"><FONT SIZE=3><I><SPAN STYLE="font-weight: normal">Quadra</SPAN>:<B>
</B></I><SPAN STYLE="font-style: normal">Informe a quadra desejada e
ser&aacute; apresentada uma lista com todas as ITBIs cadastradas para
os im&oacute;veis que pertencem a essa quadra. </SPAN></FONT></FONT>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0.2cm"><FONT FACE="Times, serif"><FONT SIZE=3><I>Lote:</I><SPAN STYLE="font-style: normal">
Informe o lote desejado e ser&aacute; apresentado uma lista com todas
as ITBIs cadastradas para os im&oacute;veis que pertencem a esse
lote.</SPAN></FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0.2cm; font-style: normal">
<FONT FACE="Times, serif"><FONT SIZE=3>	Pode-se utilizar os tr&ecirc;s
filtro mencionados (setor/quadra/lote) de forma conjugada, ou seja,
se for informado o setor, a quadra e o lote, ser&aacute; apresentada
uma listas com as ITBIs cadastrada para os im&oacute;veis do setor,
quadra e lote indicado.</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0.2cm"><FONT FACE="Times, serif"><FONT SIZE=3><I><SPAN STYLE="font-weight: normal">Logradouro</SPAN>:<B>
</B></I><SPAN STYLE="font-style: normal">Informe o c&oacute;digo do
logradouro desejado ou selecione na lista clicando no link. Ser&aacute;
apresentada uma lista contendo todas as ITBIs cadastradas para os
im&oacute;veis que estejam localizados no logradouro indicado.</SPAN></FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0.2cm; font-style: normal; font-weight: normal">
<FONT FACE="Times, serif"><FONT SIZE=3>	As op&ccedil;&otilde;es de
filtro &ndash; matr&iacute;cula do im&oacute;vel, setor, quadra,
lote, logradouro - s&oacute; estar&atilde;o ativas se o tipo de ITBI
selecionado for urbana.</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0.2cm; font-style: normal; font-weight: normal">
<FONT FACE="Times, serif"><FONT SIZE=3><I>Setor:</I> Poder&aacute;
selecionar o Setor de Localiza&ccedil;&atilde;o em que a Matricula
que foi gerado a ITBI esta vinculada.</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0.2cm; font-style: normal; font-weight: normal">
<FONT FACE="Times, serif"><FONT SIZE=3><I>Quadra:</I> Poder&aacute;
selecionar a Quadra de Localiza&ccedil;&atilde;o em que a Matricula
que foi gerado a ITBI esta vinculada.</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0.2cm; font-style: normal; font-weight: normal">
<FONT FACE="Times, serif"><FONT SIZE=3><I>Lote:</I> Poder&aacute;
selecionar o Lote de Localiza&ccedil;&atilde;o em que a Matricula que
foi gerado a ITBI esta vinculada.</FONT></FONT></P>
