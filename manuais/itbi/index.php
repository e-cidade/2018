<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0.2cm; font-weight: normal">
<FONT FACE="Times, serif"><FONT SIZE=3><I>Valor financiado do
terreno: </I><SPAN STYLE="font-style: normal">Nesse campo deve ser
informado o valor do terreno atribu&iacute;do pelo avaliador da
prefeitura correspondente a parcela da transa&ccedil;&atilde;o que
ser&aacute; financiado pelo adquirente do im&oacute;vel.</SPAN></FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0.2cm; font-weight: normal">
<FONT FACE="Times, serif"><FONT SIZE=3><I>Valor financiado da
constru&ccedil;&atilde;o: </I><SPAN STYLE="font-style: normal">Nesse
campo deve ser informado o valor da(s) constru&ccedil;&atilde;o(&otilde;es)
atribu&iacute;do pelo avaliador da prefeitura correspondente a
parcela da transa&ccedil;&atilde;o que ser&aacute; financiado pelo
