atribui o valor do im&oacute;vel objeto da transmiss&atilde;o.</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0.2cm; font-style: normal; font-weight: normal">
<FONT FACE="Times, serif"><FONT SIZE=3>	Para avaliar uma ITBI,
