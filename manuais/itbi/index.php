sendo liberada.</SPAN></FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0.2cm; font-weight: normal">
<FONT FACE="Times, serif"><FONT SIZE=3><I>Valor a vista do terreno:
</I><SPAN STYLE="font-style: normal">Nesse campo deve ser informado o
valor do terreno atribu&iacute;do pelo avaliador da prefeitura
correspondente a parcela da transa&ccedil;&atilde;o que ser&aacute;
paga a vista pelo adquirente do im&oacute;vel.</SPAN></FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0.2cm; font-weight: normal">
