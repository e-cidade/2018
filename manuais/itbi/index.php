<P CLASS="western" ALIGN=JUSTIFY><FONT FACE="Times, serif"><FONT SIZE=3>	Esse
cadastro consiste em criar uma base contendo as formas de transmiss&atilde;o
de im&oacute;veis, independentemente de ter ou n&atilde;o a
incid&ecirc;ncia do imposto.</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><FONT FACE="Times, serif"><FONT SIZE=3>	Nesse
cadastro, devem ser lan&ccedil;ados todos os item que est&atilde;o
definidos no C&oacute;digo Tribut&aacute;rio Municipal, da parte que
trata da incid&ecirc;ncia e da n&atilde;o incid&ecirc;ncia.</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><FONT FACE="Times, serif"><FONT SIZE=3><B>	Ex.:</B>
Compra e venda, cess&atilde;o de direitos contratuais, compra e venda
com usufruto, compra e venda da nu propriedade, etc.</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><BR><BR>
</P>
