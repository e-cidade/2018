       $sql  .= $virgula." h41_sequencial = $this->h41_sequencial ";
       $virgula = ",";
       if(trim($this->h41_sequencial) == null ){ 
         $this->erro_sql = " Campo Cod. Sequencial nao Informado.";
         $this->erro_campo = "h41_sequencial";
