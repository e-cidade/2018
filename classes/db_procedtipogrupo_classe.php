   // funcao para atualizar campos
   function atualizacampos($exclusao=false) {
     if($exclusao==false){
       $this->v29_sequencial = ($this->v29_sequencial == ""?@$GLOBALS["HTTP_POST_VARS"]["v29_sequencial"]:$this->v29_sequencial);
