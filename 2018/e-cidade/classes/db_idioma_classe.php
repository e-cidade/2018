   function incluir ($bi22_sequencial){ 
      $this->atualizacampos();
     if($this->bi22_idioma == null ){ 
       $this->erro_sql = " Campo Idioma não informado.";
       $this->erro_campo = "bi22_idioma";
       $this->erro_banco = "";
       $this->erro_msg   = "Usuário: \\n\\n ".$this->erro_sql." \\n\\n";
       $this->erro_msg   .=  str_replace('"',"",str_replace("'","",  "Administrador: \\n\\n ".$this->erro_banco." \\n"));
       $this->erro_status = "0";
       return false;
     }
     if($bi22_sequencial == "" || $bi22_sequencial == null ){
       $result = db_query("select nextval('idioma_bi22_sequencial_seq')"); 
       if($result==false){
         $this->erro_banco = str_replace("\n","",@pg_last_error());
         $this->erro_sql   = "Verifique o cadastro da sequencia: idioma_bi22_sequencial_seq do campo: bi22_sequencial"; 
