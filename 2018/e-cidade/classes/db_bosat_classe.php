           $resac = db_query("insert into db_acount values($acount,1460,8588,'".AddSlashes(pg_result($resaco,$conresaco,'bo03_codsat'))."','$this->bo03_codsat',".db_getsession('DB_datausu').",".db_getsession('DB_id_usuario').")");
