       $this->erro_campo = "ar15_nome";
       $this->erro_banco = "";
       $this->erro_msg   = "Usuário: \\n\\n ".$this->erro_sql." \\n\\n";
       $this->erro_msg   .=  str_replace('"',"",str_replace("'","",  "Administrador: \\n\\n ".$this->erro_banco." \\n"));
       $this->erro_status = "0";
       return false;
     }
     if($ar15_sequencial == "" || $ar15_sequencial == null ){
       $result = db_query("select nextval('cadconveniomodalidade_ar15_sequencial_seq')"); 
