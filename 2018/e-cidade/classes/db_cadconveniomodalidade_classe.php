   // funcao para atualizar campos
   function atualizacampos($exclusao=false) {
     if($exclusao==false){
       $this->ar15_sequencial = ($this->ar15_sequencial == ""?@$GLOBALS["HTTP_POST_VARS"]["ar15_sequencial"]:$this->ar15_sequencial);
