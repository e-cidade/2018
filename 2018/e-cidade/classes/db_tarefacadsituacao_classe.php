         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }else{
       $sql .= $campos;
     }
     $sql .= " from tarefacadsituacao ";
     $sql2 = "";
     if($dbwhere==""){
       if($at46_codigo!=null ){
         $sql2 .= " where tarefacadsituacao.at46_codigo = $at46_codigo "; 
       } 
     }else if($dbwhere != ""){
       $sql2 = " where $dbwhere";
     }
     $sql .= $sql2;
