   // funcao para atualizar campos
   function atualizacampos($exclusao=false) {
     if($exclusao==false){
       $this->t20_codigo = ($this->t20_codigo == ""?@$GLOBALS["HTTP_POST_VARS"]["t20_codigo"]:$this->t20_codigo);
