         $this->erro_status = "0";
         return false; 
       }
       $this->t20_codigo = pg_result($result,0,0); 
     }else{
       $result = db_query("select last_value from benstipoguarda_t20_codigo_seq");
       if(($result != false) && (pg_result($result,0,0) < $t20_codigo)){
         $this->erro_sql = " Campo t20_codigo maior que último número da sequencia.";
         $this->erro_banco = "Sequencia menor que este número.";
