       $resac = db_query("insert into db_acount values($acount,1532,8953,'','".AddSlashes(pg_result($resaco,0,'t20_descr'))."',".db_getsession('DB_datausu').",".db_getsession('DB_id_usuario').")");
     }
     return true;
   } 
