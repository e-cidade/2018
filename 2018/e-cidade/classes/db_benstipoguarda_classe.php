       $this->erro_campo = "t20_descr";
       $this->erro_banco = "";
       $this->erro_msg   = "Usuário: \\n\\n ".$this->erro_sql." \\n\\n";
       $this->erro_msg   .=  str_replace('"',"",str_replace("'","",  "Administrador: \\n\\n ".$this->erro_banco." \\n"));
       $this->erro_status = "0";
       return false;
     }
     if($t20_codigo == "" || $t20_codigo == null ){
       $result = db_query("select nextval('benstipoguarda_t20_codigo_seq')"); 
       if($result==false){
