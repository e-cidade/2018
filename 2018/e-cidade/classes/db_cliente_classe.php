         $this->erro_status = "0";
         return false; 
       }
       $this->j99_codigo = pg_result($result,0,0); 
     }else{
       $result = db_query("select last_value from cliente_j99_codigo_seq");
       if(($result != false) && (pg_result($result,0,0) < $j99_codigo)){
         $this->erro_sql = " Campo j99_codigo maior que último número da sequencia.";
         $this->erro_banco = "Sequencia menor que este número.";
