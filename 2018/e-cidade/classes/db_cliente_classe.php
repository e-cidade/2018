   // funcao para atualizar campos
   function atualizacampos($exclusao=false) {
     if($exclusao==false){
       $this->j99_codigo = ($this->j99_codigo == ""?@$GLOBALS["HTTP_POST_VARS"]["j99_codigo"]:$this->j99_codigo);
