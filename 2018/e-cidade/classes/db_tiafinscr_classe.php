       $virgula = "";
       for($i=0;$i<sizeof($campos_sql);$i++){
         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }else{
       $sql .= $campos;
     }
     $sql .= " from tiafinscr ";
     $sql .= "      inner join issbase  on  issbase.q02_inscr = tiafinscr.y94_inscr";
     $sql .= "      inner join tiaf  on  tiaf.y90_codtiaf = tiafinscr.y94_codtiaf";
     $sql .= "      inner join cgm  on  cgm.z01_numcgm = issbase.q02_numcgm";
     $sql2 = "";
     if($dbwhere==""){
       if($y94_codtiaf!=null ){
         $sql2 .= " where tiafinscr.y94_codtiaf = $y94_codtiaf "; 
       } 
     }else if($dbwhere != ""){
       $sql2 = " where $dbwhere";
     }
     $sql .= $sql2;
