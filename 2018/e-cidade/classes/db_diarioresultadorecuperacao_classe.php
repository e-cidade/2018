   function sql_query ( $ed116_sequencial=null,$campos="*",$ordem=null,$dbwhere=""){ 
     $sql = "select ";
     if($campos != "*" ){
       $campos_sql = split("#",$campos);
       $virgula = "";
       for($i=0;$i<sizeof($campos_sql);$i++){
         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }else{
       $sql .= $campos;
     }
     $sql .= " from diarioresultadorecuperacao ";
     $sql .= "      inner join diarioresultado  on  diarioresultado.ed73_i_codigo = diarioresultadorecuperacao.ed116_diarioresultado";
     $sql .= "      inner join procresultado  on  procresultado.ed43_i_codigo = diarioresultado.ed73_i_procresultado";
     $sql .= "      inner join diario  on  diario.ed95_i_codigo = diarioresultado.ed73_i_diario";
     $sql2 = "";
     if($dbwhere==""){
       if($ed116_sequencial!=null ){
         $sql2 .= " where diarioresultadorecuperacao.ed116_sequencial = $ed116_sequencial "; 
       } 
     }else if($dbwhere != ""){
       $sql2 = " where $dbwhere";
     }
     $sql .= $sql2;
     if($ordem != null ){
       $sql .= " order by ";
       $campos_sql = split("#",$ordem);
       $virgula = "";
       for($i=0;$i<sizeof($campos_sql);$i++){
         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }
     return $sql;
  }
