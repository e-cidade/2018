         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }else{
       $sql .= $campos;
     }
     $sql .= " from atendimentomod 
     				inner join db_modulos on db_modulos.id_item = atendimentomod.at08_modulo";
     $sql2 = "";
     if($dbwhere==""){
       if( $at08_atend != "" && $at08_atend != null){
          $sql2 = " where atendimentomod.at08_atend = '$at08_atend'";
       }
     }else if($dbwhere != ""){
       $sql2 = " where $dbwhere";
     }
     $sql .= $sql2;
     if($ordem != null ){
       $sql .= " order by ";
       $campos_sql = split("#",$ordem);
