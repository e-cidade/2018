   function sql_query ( $j03_matric=null,$campos="*",$ordem=null,$dbwhere=""){ 
     $sql = "select ";
     if($campos != "*" ){
       $campos_sql = split("#",$campos);
       $virgula = "";
       for($i=0;$i<sizeof($campos_sql);$i++){
         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }else{
       $sql .= $campos;
     }
     $sql .= " from iptubaixaproc ";
     $sql .= "      inner join protprocesso  on  protprocesso.p58_codproc = iptubaixaproc.j03_codproc";
     $sql .= "      inner join iptubaixa  on  iptubaixa.j02_matric = iptubaixaproc.j03_matric";
     $sql .= "      inner join cgm  on  cgm.z01_numcgm = protprocesso.p58_numcgm";
     $sql .= "      inner join db_usuarios  on  db_usuarios.id_usuario = protprocesso.p58_id_usuario";
     $sql .= "      inner join db_depart  on  db_depart.coddepto = protprocesso.p58_coddepto";
     $sql .= "      inner join tipoproc  on  tipoproc.p51_codigo = protprocesso.p58_codigo";
     $sql .= "      inner join iptubase  on  iptubase.j01_matric = iptubaixa.j02_matric";
     $sql .= "      inner join db_usuarios  as a on   a.id_usuario = iptubaixa.j02_usuario";
     $sql2 = "";
     if($dbwhere==""){
       if($j03_matric!=null ){
         $sql2 .= " where iptubaixaproc.j03_matric = $j03_matric "; 
       } 
     }else if($dbwhere != ""){
       $sql2 = " where $dbwhere";
     }
     $sql .= $sql2;
     if($ordem != null ){
       $sql .= " order by ";
       $campos_sql = split("#",$ordem);
       $virgula = "";
       for($i=0;$i<sizeof($campos_sql);$i++){
         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }
     return $sql;
  }
