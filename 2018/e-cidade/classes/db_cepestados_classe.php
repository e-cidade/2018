   }
   //funcao erro 
   function erro($mostra,$retorna) { 
     if(($this->erro_status == "0") || ($mostra == true && $this->erro_status != null )){
        echo "<script>alert(\"".$this->erro_msg."\");</script>";
        if($retorna==true){
           echo "<script>location.href='".$this->pagina_retorno."'</script>";
        }
     }
   }
   // funcao para atualizar campos
   function atualizacampos($exclusao=false) {
     if($exclusao==false){
       $this->cp03_sigla = ($this->cp03_sigla == ""?@$GLOBALS["HTTP_POST_VARS"]["cp03_sigla"]:$this->cp03_sigla);
       $this->cp03_estado = ($this->cp03_estado == ""?@$GLOBALS["HTTP_POST_VARS"]["cp03_estado"]:$this->cp03_estado);
     }else{
       $this->cp03_sigla = ($this->cp03_sigla == ""?@$GLOBALS["HTTP_POST_VARS"]["cp03_sigla"]:$this->cp03_sigla);
     }
   }
   // funcao para inclusao
   function incluir ($cp03_sigla){ 
      $this->atualizacampos();
     if($this->cp03_estado == null ){ 
       $this->erro_sql = " Campo Estado nao Informado.";
       $this->erro_campo = "cp03_estado";
       $this->erro_banco = "";
