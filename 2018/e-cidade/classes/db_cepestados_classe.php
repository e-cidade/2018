       $this->erro_status = "0";
       return false;
     }
       $this->cp03_sigla = $cp03_sigla; 
     if(($this->cp03_sigla == null) || ($this->cp03_sigla == "") ){ 
       $this->erro_sql = " Campo cp03_sigla nao declarado.";
