   public function sql_query ($oid = null, $campos = "afastaassenta.oid,*", $ordem = null, $dbwhere = "") { 

     $sql  = "select {$campos}";
     $sql .= "  from afastaassenta ";
     $sql .= "      inner join afasta  on  afasta.r45_codigo = afastaassenta.h81_afasta";
     $sql .= "      inner join assenta  on  assenta.h16_codigo = afastaassenta.h81_assenta";
     $sql .= "      inner join rhpessoal  on  rhpessoal.rh01_regist = afasta.r45_regist";
     $sql .= "      inner join db_usuarios  on  db_usuarios.id_usuario = assenta.h16_login";
     $sql .= "      inner join tipoasse  on  tipoasse.h12_codigo = assenta.h16_assent";
     $sql .= "      inner join rhpessoal  as a on   a.rh01_regist = assenta.h16_regist";
     $sql2 = "";
     if (empty($dbwhere)) {
       if (!empty($oid)) {
          $sql2 = " where afastaassenta.oid = '$oid'";
       }
     } else if (!empty($dbwhere)) {
       $sql2 = " where $dbwhere";
     }
     $sql .= $sql2;
     if (!empty($ordem)) {
       $sql .= " order by {$ordem}";
     }
     return $sql;
  }
