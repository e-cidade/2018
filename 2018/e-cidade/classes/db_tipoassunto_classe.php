   function incluir ($bi30_sequencial){ 
      $this->atualizacampos();
     if($this->bi30_descricao == null ){ 
       $this->erro_sql = " Campo Descrição não informado.";
       $this->erro_campo = "bi30_descricao";
       $this->erro_banco = "";
       $this->erro_msg   = "Usuário: \\n\\n ".$this->erro_sql." \\n\\n";
       $this->erro_msg   .=  str_replace('"',"",str_replace("'","",  "Administrador: \\n\\n ".$this->erro_banco." \\n"));
       $this->erro_status = "0";
       return false;
     }
     if($bi30_sequencial == "" || $bi30_sequencial == null ){
       $result = db_query("select nextval('tipoassunto_bi30_sequencial_seq')"); 
       if($result==false){
         $this->erro_banco = str_replace("\n","",@pg_last_error());
         $this->erro_sql   = "Verifique o cadastro da sequencia: tipoassunto_bi30_sequencial_seq do campo: bi30_sequencial"; 
