         $this->erro_status = "0";
         return false; 
       }
       $this->bi30_sequencial = pg_result($result,0,0); 
     }else{
       $result = db_query("select last_value from tipoassunto_bi30_sequencial_seq");
       if(($result != false) && (pg_result($result,0,0) < $bi30_sequencial)){
         $this->erro_sql = " Campo bi30_sequencial maior que último número da sequencia.";
         $this->erro_banco = "Sequencia menor que este número.";
