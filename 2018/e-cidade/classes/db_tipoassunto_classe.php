   public function sql_query ($bi30_sequencial = null,$campos = "*", $ordem = null, $dbwhere = "") { 

     $sql  = "select {$campos}";
     $sql .= "  from tipoassunto ";
     $sql2 = "";
     if (empty($dbwhere)) {
       if (!empty($bi30_sequencial)) {
         $sql2 .= " where tipoassunto.bi30_sequencial = $bi30_sequencial "; 
       } 
     } else if (!empty($dbwhere)) {
       $sql2 = " where $dbwhere";
     }
     $sql .= $sql2;
     if (!empty($ordem)) {
       $sql .= " order by {$ordem}";
     }
     return $sql;
  }
