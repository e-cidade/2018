   public function sql_query ($pd01_sequencial = null,$campos = "*", $ordem = null, $dbwhere = "") { 

     $sql  = "select {$campos}";
     $sql .= "  from planilhadistribuicao ";
     $sql2 = "";
     if (empty($dbwhere)) {
       if (!empty($pd01_sequencial)) {
         $sql2 .= " where planilhadistribuicao.pd01_sequencial = $pd01_sequencial "; 
       } 
     } else if (!empty($dbwhere)) {
       $sql2 = " where $dbwhere";
     }
     $sql .= $sql2;
     if (!empty($ordem)) {
       $sql .= " order by {$ordem}";
     }
     return $sql;
  }
