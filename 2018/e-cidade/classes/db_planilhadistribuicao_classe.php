   // funcao para Inclusão
   function incluir ($pd01_sequencial){ 
      $this->atualizacampos();
     if($this->pd01_descricao == null ){ 
       $this->erro_sql = " Campo Descrição não informado.";
       $this->erro_campo = "pd01_descricao";
       $this->erro_banco = "";
       $this->erro_msg   = "Usuário: \\n\\n ".$this->erro_sql." \\n\\n";
       $this->erro_msg   .=  str_replace('"',"",str_replace("'","",  "Administrador: \\n\\n ".$this->erro_banco." \\n"));
       $this->erro_status = "0";
       return false;
     }
     if($pd01_sequencial == "" || $pd01_sequencial == null ){
       $result = db_query("select nextval('planilhadistribuicao_pd01_sequencial_seq')"); 
       if($result==false){
         $this->erro_banco = str_replace("\n","",@pg_last_error());
         $this->erro_sql   = "Verifique o cadastro da sequencia: planilhadistribuicao_pd01_sequencial_seq do campo: pd01_sequencial"; 
