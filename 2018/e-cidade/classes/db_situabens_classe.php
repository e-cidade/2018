   // funcao para inclusao
   function incluir ($t70_situac){ 
      $this->atualizacampos();
     if($this->t70_descr == null ){ 
       $this->erro_sql = " Campo Descrição da situação nao Informado.";
       $this->erro_campo = "t70_descr";
       $this->erro_banco = "";
       $this->erro_msg   = "Usuário: \\n\\n ".$this->erro_sql." \\n\\n";
       $this->erro_msg   .=  str_replace('"',"",str_replace("'","",  "Administrador: \\n\\n ".$this->erro_banco." \\n"));
       $this->erro_status = "0";
       return false;
     }
     if($t70_situac == "" || $t70_situac == null ){
       $result = db_query("select nextval('situabens_t70_situac_seq')"); 
       if($result==false){
         $this->erro_banco = str_replace("\n","",@pg_last_error());
         $this->erro_sql   = "Verifique o cadastro da sequencia: situabens_t70_situac_seq do campo: t70_situac"; 
