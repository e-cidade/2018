       $resac = db_query("insert into db_acount values($acount,926,5792,'','".AddSlashes(pg_result($resaco,0,'t70_descr'))."',".db_getsession('DB_datausu').",".db_getsession('DB_id_usuario').")");
     }
     return true;
   } 
