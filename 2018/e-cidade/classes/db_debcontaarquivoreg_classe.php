   function sql_query ( $d73_sequencial=null,$campos="*",$ordem=null,$dbwhere=""){ 
     $sql = "select ";
     if($campos != "*" ){
       $campos_sql = split("#",$campos);
       $virgula = "";
       for($i=0;$i<sizeof($campos_sql);$i++){
         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }else{
       $sql .= $campos;
     }
     $sql .= " from debcontaarquivoreg ";
     $sql .= "      inner join debcontaarquivo  on  debcontaarquivo.d72_codigo = debcontaarquivoreg.d73_codigo";
     $sql .= "      inner join arretipo  on  arretipo.k00_tipo = debcontaarquivo.d72_arretipo";
     $sql .= "      inner join bancos  on  bancos.codbco = debcontaarquivo.d72_banco";
     $sql2 = "";
     if($dbwhere==""){
       if($d73_sequencial!=null ){
         $sql2 .= " where debcontaarquivoreg.d73_sequencial = $d73_sequencial "; 
       } 
     }else if($dbwhere != ""){
       $sql2 = " where $dbwhere";
     }
     $sql .= $sql2;
     if($ordem != null ){
       $sql .= " order by ";
       $campos_sql = split("#",$ordem);
       $virgula = "";
       for($i=0;$i<sizeof($campos_sql);$i++){
         $sql .= $virgula.$campos_sql[$i];
         $virgula = ",";
       }
     }
     return $sql;
  }
