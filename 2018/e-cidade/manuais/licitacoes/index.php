<P CLASS="western" ALIGN=JUSTIFY><FONT FACE="arial, sans-serif"><I><B>Sobre
o tipos de julgamentos de licita&ccedil;&otilde;es no sistema:</B></I></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><FONT FACE="arial, sans-serif"><I><B>a)
Julgamento por Item:</B></I></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 1.98cm; font-style: normal; font-weight: medium">
<FONT FACE="arial, sans-serif">Neste tipo de julgamento o sistema ir&aacute;
analisar as cota&ccedil;&otilde;es lan&ccedil;adas pelos fornecedores
a cada um dos itens contidos na licita&ccedil;&atilde;o. Feito isso,
o julgamento se dar&aacute; pelo menor pre&ccedil;o.Desta forma, cada
item ser&aacute; julgado isoladamente, sendo declarado vencedor
aquela fornecedor que possuir a menor cota&ccedil;&atilde;o.</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 1.98cm; font-style: normal; font-weight: medium">
<FONT FACE="arial, sans-serif">Em caso de empate entre fornecedores,
o sistema trar&aacute; como vencedor, por conven&ccedil;&atilde;o,
aquele que foi primeiramente cadastrado na licita&ccedil;&atilde;o
que se est&aacute; realizando.</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 1.98cm; font-style: normal; font-weight: medium">
<FONT FACE="arial, sans-serif">Vale lembrar que o crit&eacute;rio de
desempate ser&aacute; definido no edital, sendo o crit&eacute;rio
utilizado pelo sistema uma mera conven&ccedil;&atilde;o.</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><FONT FACE="arial, sans-serif"><I><B>b)
Julgamento Global:</B></I></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; font-style: normal; font-weight: medium">
<FONT FACE="arial, sans-serif">O crit&eacute;rio utilizado no
julgamento global leva em considera&ccedil;&atilde;o os valores
totais cotados por cada um dos fornecedores. Assim, ser&aacute;
declarado vencedor aquele que cotar o menor valor global.</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; font-style: normal; font-weight: medium">
<FONT FACE="arial, sans-serif">Cumpre esclarecer que o fornecedor
somente participar&aacute; do julgamento, caso tenha cotado valores
para todos os itens contidos na licita&ccedil;&atilde;o. Deixando de
cotar ser&aacute; desclassificado do certame.</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; font-style: normal">
<FONT FACE="arial, sans-serif"><B>c) Julgamento por Lote:</B></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; font-style: normal; font-weight: medium">
<FONT FACE="arial, sans-serif">Neste processo o sistema ir&aacute;
analisar cada um dos lotes criados na licita&ccedil;&atilde;o,
procedendo o julgamento dos mesmos de forma isolada.</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; font-style: normal; font-weight: medium">
<FONT FACE="arial, sans-serif">Assim, ser&aacute; declarado vencedor
de um determinado lote aquele fornecedor que cotou o menor pre&ccedil;o
para o lote que se est&aacute; julgando.</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; font-style: normal; font-weight: medium">
<FONT FACE="arial, sans-serif">Cumpre esclarecer que o fornecedor
somente participar&aacute; do julgamento, caso tenha cotado valores
para todos os itens contidos no lote. Deixando de cotar ser&aacute;
desclassificado do certame.</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; font-style: normal; font-weight: medium">
<FONT FACE="arial, sans-serif">Preenchidos os campos, confirma-se a
inclus&atilde;o da licita&ccedil;&atilde;o clicando no bot&atilde;o
