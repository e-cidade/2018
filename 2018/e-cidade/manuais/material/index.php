<P CLASS="western"><FONT FACE="Arial, sans-serif">Vale lembrar que
todo o material oriundo do cadastro do compras dever&aacute; ter um
ou mais materiais do m&oacute;dulo material ligado a ele, e
vice-versa.</FONT></P>
