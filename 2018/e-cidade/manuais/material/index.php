<H3 CLASS="western"><A NAME="__RefHeading__5164_1490193190"></A><A HREF="#Sum&aacute;rio1|region" NAME="db4315db">Cancelar</A></H3>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; font-style: normal; font-weight: normal; text-decoration: none">
<FONT FACE="arial, sans-serif">Somente o dep&oacute;sito origem
poder&aacute; efetivar o cancelamento de uma transfer&ecirc;ncia,
desde que o dep&oacute;sito destino n&atilde;o tenha confirmado a
mesma.</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; font-style: normal; font-weight: normal; text-decoration: none">
<FONT FACE="arial, sans-serif">Na tela que se abrir&aacute;, o
usu&aacute;rio dever&aacute; informar ao sistema o c&oacute;digo do
lan&ccedil;amento para que o mesmo proceda a pesquisa.</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; font-style: normal; font-weight: normal; text-decoration: none">
<FONT FACE="arial, sans-serif">Selecionado o lan&ccedil;amento a nova
tela trar&aacute; as seguintes informa&ccedil;&otilde;es: c&oacute;digo
do lan&ccedil;amento, departamento de origem, departamento destino,
observa&ccedil;&atilde;o do lan&ccedil;amento e uma tabela contendo
c&oacute;digo, c&oacute;digo e descri&ccedil;&atilde;o do material e
quantidade.</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; font-style: normal; font-weight: normal; text-decoration: none">
<FONT FACE="arial, sans-serif">Para efetivar o cancelamento da
transfer&ecirc;ncia, basta o usu&aacute;rio clicar no bot&atilde;o
[cancelar transfer&ecirc;ncia].</FONT></P>
