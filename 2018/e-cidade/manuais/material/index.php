estoque. Assim, caso houver a necessidade de se cadastrar um material
novo no cadastro de materiais do m&oacute;dulo material, basta o
usu&aacute;rio clicar neste bot&atilde;o para que o cadastro fique a
disposi&ccedil;&atilde;o do mesmo. </FONT>
</P>
<P CLASS="western"><FONT FACE="Arial, sans-serif">O bot&atilde;o
escolher serve para o usu&aacute;rio selecionar um material contido
no cadastro de materiais. Esta rotina &eacute; utilizada naqueles
casos em que h&aacute; um material previamente cadastrado, mas que no
entanto, n&atilde;o possui liga&ccedil;&atilde;o com o material que
est&aacute; contido na ordem de compra. </FONT>
