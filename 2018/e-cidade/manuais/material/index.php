<P CLASS="western"><FONT FACE="Arial, sans-serif"><B>Bot&otilde;es
Salvar, Novo e Escolher</B></FONT></P>
<P CLASS="western"><FONT FACE="Arial, sans-serif">O bot&atilde;o
salvar serve para confirmar as rotinas realizadas nos &ldquo;Dados do
Lan&ccedil;amento&rdquo;.  Assim, a cada lan&ccedil;amento de
