<H2 CLASS="western"><A NAME="__RefHeading__5122_1490193190"></A><A HREF="#Sum&aacute;rio1|region" NAME="db4368db">Manuten&ccedil;&atilde;o
de Par&acirc;metros</A></H2>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 1.98cm"><FONT FACE="arial, sans-serif">Par&acirc;metros
a serem configurados antes ou durante da implanta&ccedil;&atilde;o do
m&oacute;dulo. Estes par&acirc;metros definem a forma de movimenta&ccedil;&atilde;o
dos materiais no estoque.</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><FONT FACE="arial, sans-serif"><B>Tipo
de Controle de Estoque</B></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2cm; font-weight: normal">
<FONT FACE="arial, sans-serif">Este par&acirc;metro n&atilde;o pode
ser modificado depois do in&iacute;cio da utiliza&ccedil;&atilde;o do
m&oacute;dulo. Fazer isso provavelmente causar&aacute; danos aos
registros j&aacute; existentes. O mais indicado &eacute; zerar o
estoque e reimplantar o mesmo caso necessite modificar este
par&acirc;metro.</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><FONT FACE="arial, sans-serif"><B>Por
almoxarifado/dep&oacute;sito</B></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 1.98cm; font-weight: normal">
<FONT FACE="arial, sans-serif">A administra&ccedil;&atilde;o p&uacute;blica
define que alguns departamentos existentes ser&atilde;o considerados
dep&oacute;sitos.</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 1.98cm; font-weight: normal">
<FONT FACE="arial, sans-serif">Ex.: Almoxarifado central,
almoxarifado sa&uacute;de, almoxarifado educa&ccedil;&atilde;o.</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="font-weight: normal"><FONT FACE="arial, sans-serif">No
procedimento de inclus&atilde;o de ordem de compra: vai obrigar
sempre a fazer a ordem de compra vinculada a um
deposito/almoxarifado, pois os dep&oacute;sitos j&aacute; ir&atilde;o
aparecer, sem que o usu&aacute;rio possa escolher um departamento que
n&atilde;o for dep&oacute;sito. Se n&atilde;o existir
dep&oacute;sito/almoxarifado cadastrado, o sistema n&atilde;o deixar&aacute;
fazer uma ordem de compra.</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><FONT FACE="arial, sans-serif"><SPAN STYLE="font-weight: normal">No
Procedimento de e</SPAN>ntrada da ordem de compra o sistema verifica
se o departamento no qual o usu&aacute;rio est&aacute; logado
encontra-se vinculado ao deposito/almoxarifado da ordem de compra. Em
caso positivo, o sistema ir&aacute; liberar a entrada da ordem de
compra, sendo o material lan&ccedil;ado no estoque do dep&oacute;sito
da ordem de compra, independente do departamento em que o usu&aacute;rio
se encontra logado.</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><FONT FACE="arial, sans-serif"><B>Por
Almoxarifado/departamento</B></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; font-weight: normal">
<FONT FACE="arial, sans-serif">A administra&ccedil;&atilde;o p&uacute;blica
define que todos os departamentos tamb&eacute;m ser&atilde;o
considerados dep&oacute;sitos.</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; font-weight: normal">
<FONT FACE="arial, sans-serif">Ex.: Almoxarifado central,
almoxarifado secretaria da educa&ccedil;&atilde;o, almoxarifado
secretaria administra&ccedil;&atilde;o, almoxarifado secretaria de
obras.</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><FONT FACE="arial, sans-serif"><SPAN STYLE="font-weight: normal">No
procedimento de inclus&atilde;o de ordem de compra, </SPAN>vai sempre
fazer a ordem de compra por departamento, independente de ser
dep&oacute;sito ou n&atilde;o, trazendo por padr&atilde;o o
departamento na qual o usu&aacute;rio est&aacute; logado, por&eacute;m
permitindo que este troque o departamento onde o material ser&aacute;
entregue.</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><FONT FACE="arial, sans-serif"><B>Almoxarifado
da Ordem de Compra</B></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 1.98cm; font-weight: normal">
<FONT FACE="arial, sans-serif">Escolhido o par&acirc;metro
departamento do usu&aacute;rio no momento de gerar a ordem de compra,
o sistema trar&aacute; o departamento no qual o usu&aacute;rio est&aacute;
logado para indica&ccedil;&atilde;o do dep&oacute;sito a ser
direcionada a ordem de compra. Neste caso, o usu&aacute;rio dever&aacute;
proceder a escolha do dep&oacute;sito. Quando escolhido o par&acirc;metro
departamento de origem, o sistema trar&aacute; o dep&oacute;sito no
qual o depto requisitante est&aacute; vinculado. </FONT>
</P>
<P CLASS="western" ALIGN=JUSTIFY><FONT FACE="arial, sans-serif"><B>Fazer
requisi&ccedil;&atilde;o sem estoque</B></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm"><FONT FACE="arial, sans-serif">Par&acirc;metro
relacionado as inclus&otilde;es de requisi&ccedil;&otilde;es de sa&iacute;da
