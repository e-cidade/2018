<P CLASS="western"><FONT FACE="Arial, sans-serif"><B>Bot&atilde;o
Fracionar</B></FONT></P>
<P CLASS="western"><FONT FACE="Arial, sans-serif">Utilizado para
fracionar as quantidades de um determinado material, distribuindo-o
em dois os mais materiais em estoque.</FONT></P>
<P CLASS="western"><FONT FACE="Arial, sans-serif">Ex.: Foram
adquiridas 10 caixas de canetas. Essas caixas poder&atilde;o ser
distribu&iacute;das em dois ou mais materiais em estoque. Assim 5
caixas podem ingressar no estoque de canetas pretas e as demais no
estoque das canetas vermelhas.</FONT></P>
<P CLASS="western"><FONT FACE="Arial, sans-serif">Para isso, deve o
usu&aacute;rio escolher um material na tabela &ldquo;itens da ordem&rdquo;.
Habilitado o material, deve o usu&aacute;rio indicar inicialmente a
quantidade que quer seja fracionada para um determinado material em
estoque. Inseridas todas as informa&ccedil;&otilde;es de entrada,
deve o usu&aacute;rio clicar no bot&atilde;o fracionar. Feito isso, a
quantidade restante do item adquirido ir&aacute; aparecer no campo
quantidade recebida contida nos &ldquo;Dados do Lan&ccedil;amento&rdquo;.
Escolhe-se outro material e a quantidade a ser lan&ccedil;ada.
Preenchidos os dados, clica-se novamente no &ldquo;bot&atilde;o
fracionar&rdquo;. Essa opera&ccedil;&atilde;o ir&aacute; continuar
enquanto houveram quantidades a serem lan&ccedil;adas no estoque.</FONT></P>
<P CLASS="western"><FONT FACE="Arial, sans-serif">Informado ao
sistema todos os dados contidos na nota fiscal, bem como, efetuados
todos os lan&ccedil;amentos de materiais no estoque, encerra-se o
processo clicando no bot&atilde;o &ldquo;confirmar&rdquo;.</FONT></P>
<P CLASS="western"><FONT FACE="Arial, sans-serif">&Eacute; atrav&eacute;s
da confirma&ccedil;&atilde;o que os materiais ir&atilde;o ingressar
efetivamente no estoque do dep&oacute;sito indicado na ordem de
compra.</FONT></P>
