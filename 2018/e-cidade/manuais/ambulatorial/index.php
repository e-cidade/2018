<H2 CLASS="western"><A NAME="__RefHeading__4859_1794488960"></A><A CLASS="western" HREF="#Sum&aacute;rio1_Head|region" NAME="db1045399db">CGS</A></H2>
<P STYLE="margin-bottom: 0in">	Este cadastro consiste em criar uma
base de dados dos pacientes de todo o munic&iacute;pio, permitindo
interligar todas as unidades no mesmo Cadastro Geral da Sa&uacute;de.
O CGS &eacute; o cadastro mais importante desse m&oacute;dulo e da
&aacute;rea do sistema de sa&uacute;de, pois todos os dados converte
para o hist&oacute;rico de dados do paciente. Para fazer inclus&otilde;es
nesse cadastro, preencha os seguinte campos.</P>
<P ALIGN=JUSTIFY><BR><BR>
</P>
<H3 CLASS="western"><A NAME="__RefHeading__4861_1794488960"></A><A CLASS="western" HREF="#Sum&aacute;rio1_Head|region" NAME="db1045410db">Inclus&atilde;o</A></H3>
<P ALIGN=JUSTIFY><B>ABA Dados Pessoais</B></P>
<P STYLE="margin-bottom: 0in"><I><B>	CGS: </B></I>O campo CGS &ndash;
Cadastro Geral da Sa&uacute;de ser&aacute; gerado automaticamente e
corresponde ao c&oacute;digo do item que est&aacute; sendo inclu&iacute;do.</P>
<P STYLE="margin-bottom: 0in"><I><B>	CGS do Munic&iacute;pio: </B></I>O
campo CGS do Munic&iacute;pio indica se o Paciente reside ou n&atilde;o
no munic&iacute;pio, se n&atilde;o desabilita as &acirc;ncoras CEP ,
ENDERE&Ccedil;O e BAIRRO.</P>
<P STYLE="margin-left: 0.49in; margin-bottom: 0in"><I><B>CPF: </B></I>O
campo CPF &ndash; <FONT COLOR="#000000"><SPAN STYLE="font-weight: normal"><SPAN STYLE="background: transparent">Cadastro
de Pessoa F&iacute;sica </SPAN></SPAN></FONT>n&atilde;o &eacute;
obrigat&oacute;rio, mas serve como campo de pesquisa para o CGS,
ent&atilde;o &eacute; importante ser preenchido. Este campo tamb&eacute;m
serve com chave no caso de cadastro duplicado de CGS, n&atilde;o
permite cadastrar dois com o mesmo CPF.</P>
<P STYLE="margin-bottom: 0in"><I><B>	Nome: </B></I>&Eacute;
cadastrado o nome do paciente, permitindo sua identifica&ccedil;&atilde;o.
Este campo &eacute; obrigat&oacute;rio.</P>
<P STYLE="margin-bottom: 0in"><I><B>	Pai : </B></I>Neste campo deve
ser cadastrado o nome do pai do paciente, n&atilde;o sendo uma chave
para inclus&atilde;o do CGS e n&atilde;o &eacute; obrigat&oacute;rio.</P>
<P STYLE="margin-bottom: 0in"><I><B>	M&atilde;e:</B></I> <SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Neste
campo deve ser cadastrado o nome do m&atilde;e do paciente, sendo
usada como </SPAN></SPAN><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">uma
chave para inclus&atilde;o do CGS junto com a data de nascimento,
ent&atilde;o este </SPAN></SPAN><SPAN STYLE="font-style: normal"><U><SPAN STYLE="font-weight: normal">&eacute;
um campo obrigat&oacute;rio no sistema.</SPAN></U></SPAN></P>
<P STYLE="margin-bottom: 0in"><I><B>	Data Nascimento:</B></I> <SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Nesse
campo &eacute; cadastrado a data de nascimento do CGS, que &eacute;
</SPAN></SPAN><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">campo
obrigat&oacute;rio no sistema e chave conjunta com o nome da m&atilde;e
para o CGS. Ent&atilde;o o sistema n&atilde;o permite cadastrar um
CGS que possui o mesmo nome, nome da m&atilde;e e data de nascimento.</SPAN></SPAN></P>
<P STYLE="margin-bottom: 0in"><I><B>	Sexo:</B></I> <SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Selecionar
neste campo o sexo do paciente, que foi buscado os tipos do governo
do minist&eacute;rio da sa&uacute;de.</SPAN></SPAN></P>
<P STYLE="margin-bottom: 0in"><I><B>	Estado Civil: </B></I>Selecionar
neste campo o estado civil do paciente, que foi buscado os tipos do
governo do minist&eacute;rio da sa&uacute;de.</P>
<P STYLE="margin-bottom: 0in"><I><B>	Cart&atilde;o SUS: </B></I>Neste
campo &eacute; cadastrado o n&uacute;mero do cart&atilde;o do SUS que
o paciente recebe ao ser cadastrado no cadastro de sa&uacute;de do
governo. Este n&uacute;mero &eacute; muito importante, pois algumas
rotina poder&atilde;o ser feitas apenas se tiver cadastrado este
c&oacute;digo.</P>
<P STYLE="margin-bottom: 0in"><I><B>	Endere&ccedil;o Pessoal: </B></I>Nestes
campos devem ser preenchidos o endere&ccedil;o residencial do CGS,
este endere&ccedil;o serve para identificar se o paciente reside no
munic&iacute;pio ou n&atilde;o. Caso o paciente n&atilde;o perten&ccedil;a
ao programa de sa&uacute;de da fam&iacute;lia (PSF) n&atilde;o tem
endere&ccedil;o no munic&iacute;pio.</P>
<P STYLE="margin-bottom: 0in"><BR>
</P>
