opcional, pois existem CBO que n&atilde;o possuem um &oacute;rg&atilde;o
regulador. </SPAN></SPAN>
</P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>&Oacute;rg&atilde;o
Emissor:</B></I> <SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Cadastrar
o c&oacute;digo do &oacute;rg&atilde;o regulador caso o campo
registro acima for preenchido, estes c&oacute;digos s&atilde;o
importados de um lista certificada pelo governo federal, ou clicar no
link para pesquisar o c&oacute;digo.</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>CBO:</B></I>
<SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Cadastrar
o c&oacute;digo da CBO (especialidade) do profissional da &aacute;rea
que ele atua, ou clicar no link de pesquisa para identificar o CBO.
Na fun&ccedil;&atilde;o</SPAN></SPAN><FONT COLOR="#000000"><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal"><SPAN STYLE="background: transparent">
</SPAN></SPAN></SPAN></FONT><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">de
pesquisa devemos cadastrar apenas as CBOs com o tipo 4 (quatro), pois
o CNES trabalha com as CBOs </SPAN></SPAN><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">sint&eacute;ticas
e n&atilde;o com as anal&iacute;ticas.   </SPAN></SPAN>
</P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Atividade
Principal</B></I></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>V&iacute;nculo:
</B></I>Cadastrar o v&iacute;nculo empregat&iacute;cio do
profissional com a unidade de sa&uacute;de, os dados do v&iacute;nculos
(v&iacute;nculo empregat&iacute;cio, aut&ocirc;nomo, cooperativa e
outros)  s&atilde;o do programa do CNES do governo federal . 
</P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Tipo de
V&iacute;nculo:</B></I> <SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Cadastrar
o tipo de v&iacute;nculo empregat&iacute;cio do profissional com a
unidade de sa&uacute;de, os dados do tipo (estatut&aacute;rio, cargo
comissionado, celetista e outros)  s&atilde;o do programa do CNES do
governo federal. </SPAN></SPAN>
</P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Sub Tipo:
</B></I>Cadastrar o subtipo do profissional com a unidade de sa&uacute;de,
os dados do tipo (sem subtipo, contrato por ONG, contrato por
entidade filantr&oacute;pica)  s&atilde;o do programa do CNES do
governo federal. 
</P>
