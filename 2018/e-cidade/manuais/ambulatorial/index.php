Eletr&ocirc;nico</B></SPAN></I></FONT></FONT></FONT></A></H2>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in">Nesta op&ccedil;&atilde;o
&eacute; gerado o hist&oacute;rico do paciente no sistema (Prontu&aacute;rio
Eletr&ocirc;nico), pode ser digitado as fichas preenchidas
manualmente dos hist&oacute;ricos antigos e as fichas novas deve
atualizar automaticamente o hist&oacute;rico do paciente.</P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><BR>
</P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><IMG SRC="Manual_Ambulatorial_html_799377da.png" NAME="figura27" ALIGN=LEFT WIDTH=605 HEIGHT=120 BORDER=0><BR>
</P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in; font-style: normal; font-weight: normal">
