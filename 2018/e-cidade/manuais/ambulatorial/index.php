<H2 CLASS="western"><A NAME="__RefHeading__5209_1794488960"></A><A CLASS="western" HREF="#Sum&aacute;rio1_Head|region" NAME="db6825db">Triagem</A></H2>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in">Esta op&ccedil;&atilde;o
