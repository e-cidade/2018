quantidades de atendimentos por profissional.</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>CBO:</B></I>
<SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Nesse
campo &eacute; cadastrado autom&aacute;tico caso o profissional
possua apenas uma especialidade, no caso ele possua mais de uma o
usu&aacute;rio deve selecionar a especialidade do profissional.</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in; font-style: normal; font-weight: normal">
