</P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Unidade:
</B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">E</SPAN></SPAN>ste
campo &eacute; preenchido autom&aacute;tico com o c&oacute;digo do
departamento que o usu&aacute;rio est&aacute; logado no sistema,
podendo ser alterado para outra UPS.</P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>CGS:</B></I>
E<SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">ste
campo &eacute; cadastrado o c&oacute;digo do paciente para digita&ccedil;&atilde;o
do prontu&aacute;rio, e cada paciente possui apenas um prontu&aacute;rio
eletr&ocirc;nico no sistema independente se digitar para outra UPS,
pois a troca &eacute; para buscar os profissionais pertencente aquela
unidade.</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><SPAN STYLE="text-decoration: none"><B>Micro
&Aacute;rea: </B></SPAN></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Este
campo vem preenchido caso o CGS estiver com o cadastro atualizado,
sen&atilde;o pode ser cadastrado a &aacute;rea,  pois o sistema
atualiza o cadastro de CGS. </SPAN></SPAN>
</P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Fam&iacute;lia:
</B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Este
campo vem preenchido caso o CGS estiver com o cadastro atualizado,
sen&atilde;o pode ser cadastrado a fam&iacute;lia,  pois o sistema
atualiza o cadastro de CGS. </SPAN></SPAN>
</P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>M&eacute;dico:</B></I>
<SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Neste
campo deve cadastrar o profissional que realizou o atendimento
m&eacute;dico, mas como este cadastro &eacute; feito manualmente
atrav&eacute;s dos prontu&aacute;rios antigos, pode ter atendimentos
que os profissionais n&atilde;o tenham mais  vinculo com o munic&iacute;pio,
ent&atilde;o este campo n&atilde;o &eacute; obrigat&oacute;rio.</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Data:</B></I>
<SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Neste
campo preencher a data do atendimento m&eacute;dico, &eacute;
obrigat&oacute;rio.</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Hora: </B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Neste
campo preencher a hora do atendimento m&eacute;dico, &eacute;
obrigat&oacute;rio.</SPAN></SPAN><I><B> </B></I>
</P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Descri&ccedil;&atilde;o:
</B></I>Este campo &eacute; cadastrado todos os dados da consulta
feita pelo profissional da sa&uacute;de, como os dados da triagem e
do diagn&oacute;stico m&eacute;dico e o CID da consulta.</P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><BR>
