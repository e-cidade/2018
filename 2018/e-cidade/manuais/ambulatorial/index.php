e Manuten&ccedil;&atilde;o</A></B></FONT></FONT></FONT></H3>
<P ALIGN=JUSTIFY><B>ABA Paciente</B></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>FAA: </B></I>Este
campo ser&aacute; preenchido automaticamente ap&oacute;s clicar no
Bot&atilde;o Prosseguir e corresponde ao c&oacute;digo da FAA que
est&aacute; sendo inclu&iacute;do.</P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>UPS:</B></I>
<SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Esse
campo ser&aacute; buscado automaticamente do departamento que o
usu&aacute;rio est&aacute; logado no sistema.</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>CGS:</B></I>
<SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Cadastrar
o c&oacute;digo do CGS do paciente ou o usu&aacute;rio pode </SPAN></SPAN><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">clicar
no link do CGS e fazer uma pesquisa atrav&eacute;s das op&ccedil;&otilde;es
(C&oacute;digo, Nome, Cart&atilde;o SUS, Fam&iacute;lia, Micro &Aacute;rea).
Nesse procedimento o importante que no lan&ccedil;amento de uma FAA o
sistema faz uma atualiza&ccedil;&atilde;o nos dados do cadastro de
CGS, ent&atilde;o ao preencher alguns dados que n&atilde;o est&atilde;o
no </SPAN></SPAN><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">cadastro
ou alterar o endere&ccedil;o ficar&aacute; atualizado o CGS do
paciente.</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Caso
n&atilde;o possua o CGS pode ser digitado o nome do paciente e
preencher os outros dados que o sistema cria um CGS novo para o
atendimento. </SPAN></SPAN>Depois desse procedimentos realizados
acima o usu&aacute;rio deve clicar no bot&atilde;o prosseguir para
gerar o c&oacute;digo no campo da FAA, indo para a ABA Triagem.</P>
