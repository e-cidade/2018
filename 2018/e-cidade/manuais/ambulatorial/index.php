</P>
<P STYLE="margin-bottom: 0in"><B>ABA Outros Dados</B></P>
<P STYLE="margin-bottom: 0in"><I><B>	Micro:</B></I> <SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Selecionar
a qual micro regi&atilde;o o paciente pertence no munic&iacute;pio,
de acordo com o PSF - Programa de Sa&uacute;de da Fam&iacute;lia ,
algumas prefeituras os pacientes t&ecirc;m alguns benef&iacute;cios
se </SPAN></SPAN><FONT COLOR="#000000"><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal"><SPAN STYLE="background: transparent">estiverem
vinculados</SPAN></SPAN></SPAN></FONT><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">
ao programa, por isso &eacute; uma informa&ccedil;&atilde;o
importante no sistema.</SPAN></SPAN></P>
<P STYLE="margin-bottom: 0in"><I><B>	Fam&iacute;lia:</B></I> <SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Para
selecionar uma fam&iacute;lia precisa estar vinculada a uma micro,
neste caso estar no PSF, e o sistema s&oacute; busca as fam&iacute;lias
que pertence a micro selecionada no campo </SPAN></SPAN><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">anterior.
 </SPAN></SPAN>
</P>
<P STYLE="margin-bottom: 0in"><I><B>	Fator RH: </B></I>Cadastrar o
fator RH do paciente, esta informa&ccedil;&atilde;o somente deve ser
preenchida a partir de um exame ou carteirinha que comprove o fator
sangu&iacute;neo.</P>
<P STYLE="margin-bottom: 0in"><I><B>	Tipo Sangu&iacute;neo: </B></I>Cadastrar
o tipo sangu&iacute;neo do paciente, esta informa&ccedil;&atilde;o
somente deve ser preenchida a partir de um exame ou carteirinha que
comprove do tipo sangu&iacute;neo.</P>
<P STYLE="margin-bottom: 0in"><I><B>	Nome do Respons&aacute;vel:
</B></I>Cadastrar o nome do respons&aacute;vel do paciente, caso for
menor de idade ou com algum tipo de defici&ecirc;ncia.</P>
<P STYLE="margin-bottom: 0in"><I><B>	Ra&ccedil;a: </B></I>Selecionar
neste campo a ra&ccedil;a do paciente, que foi buscado os tipos do
governo do minist&eacute;rio da sa&uacute;de.</P>
<P STYLE="margin-bottom: 0in"><I><B>	Bolsa Fam&iacute;lia: </B></I>Selecionar
se o usu&aacute;rio participa do programa bolsa fam&iacute;lia do
governo federal, este cadastro vai permitir este CGS ganhar alguns
benef&iacute;cios no M&oacute;dulo  S&oacute;cio Econ&ocirc;mico.</P>
<P STYLE="margin-bottom: 0in"><I><B>	Observa&ccedil;&otilde;es:</B></I>Cadastrar
alguma informa&ccedil;&atilde;o peculiar do CGS, no caso de alguns
relat&oacute;rios do sistema de sa&uacute;de sa&iacute;ra esta
informa&ccedil;&atilde;o. Estas informa&ccedil;&otilde;es ser&atilde;o
atualizadas pelo m&eacute;dico na Consulta M&eacute;dica no campo
Fatores de Risco.</P>
<P STYLE="margin-bottom: 0in"><I><B>	Foto: </B></I>Permite ser
anexado a foto do paciente no CGS para permitir uma melhor
identifica&ccedil;&atilde;o.</P>
