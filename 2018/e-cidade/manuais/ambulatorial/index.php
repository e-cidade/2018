</B></I>Este campo deve ser cadastrado a UPS que o profissional da
sa&uacute;de vai fazer o atendimento m&eacute;dico. Um profissional
pode trabalhar em mais de um unidade e quantidade de horas
diferentes.</P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Registro:</B></I>
<SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Este
campo deve ser preenchido com o registro do &oacute;rg&atilde;o
