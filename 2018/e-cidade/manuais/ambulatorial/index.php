recepcionistas (atendentes) registram as FAA's primeiro e depois 
passam para os Enfermeiras ou auxiliares as FAA's  para realizar a
triagem do paciente.   
</P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in">S&oacute; pode
registrar as triagens de FAA's j&aacute; emitidas. Para ter acesso as
FAAs cadastradas tem que clicar no bot&atilde;o Triagem, da&iacute;
abrir&aacute; uma janela de pesquisa aonde poder&aacute; escolher as
FAA's que n&atilde;o tiveram triagem, que esta demonstrado na Figura
27 . O sistema s&oacute; mostra na Pesquisa as FAA's  que o campo
motivo n&atilde;o foi preenchido e est&aacute; digitado &ldquo;N&rdquo;.</P>
