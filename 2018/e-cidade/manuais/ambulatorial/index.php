<P ALIGN=JUSTIFY STYLE="font-style: normal"><B>ABA Triagem</B></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Na
triagem s&atilde;o analisadas pelos m&eacute;dicos plantonistas, </SPAN></SPAN>&eacute;
examinada a press&atilde;o arterial, o peso e a temperatura.</P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Motivo:
</B></I>Cadastrar o motivo da consulta do paciente, para ter uma
pr&eacute;via dos sintomas do paciente para  o atendimento do m&eacute;dico.</P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Press&atilde;o:
</B></I><SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Cadastrar
a press&atilde;o do paciente para a consulta, forma de preenchimento
(120/80 PA).</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Peso:</B></I>
<SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Cadastrar
o peso do paciente para a consulta, forma de preenchimento (57.600
KG).</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Temperatura:</B></I>
<SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Cadastrar
a temperatura do paciente para a consulta, forma de preenchimento (37
&deg;C).</SPAN></SPAN></P>
<P STYLE="text-indent: 0.79in; margin-bottom: 0in"><I><B>Profissional:</B></I>
<SPAN STYLE="font-style: normal"><SPAN STYLE="font-weight: normal">Cadastrar
