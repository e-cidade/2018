de uma rotina do sistema &eacute; o ISSQN, que possui m&oacute;dulo
para efetuar seu controle e lan&ccedil;amento.</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.01cm; margin-bottom: 0cm">
<FONT FACE="Times, serif"><FONT SIZE=3>Podemos citar como exemplos de
d&eacute;bitos que podem ser lan&ccedil;ados no m&oacute;dulo
