<H2 CLASS="western" ALIGN=JUSTIFY><A NAME="__RefHeading__1024_638646037"></A>
<A HREF="#Sum&aacute;rio1_Head|region" NAME="db5196db"><FONT FACE="Times, serif"><FONT SIZE=3>Proced&ecirc;ncia</FONT></FONT></A></H2>
<P CLASS="western" ALIGN=JUSTIFY><BR><BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY><FONT FACE="Times, serif"><FONT SIZE=3><B>ABA
PROCED&Ecirc;NCIA DOS DIVERSOS</B></FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.04cm; margin-bottom: 0cm">
<FONT FACE="Times, serif"><FONT SIZE=3>O cadastro de preced&ecirc;ncias
&eacute; a identifica&ccedil;&atilde;o da origem do d&eacute;bito, ou
seja, &eacute; atrav&eacute;s da proced&ecirc;ncia que podemos
identificar os motivos dos lan&ccedil;amentos dos d&eacute;bitos
nesse m&oacute;dulo. Seria semelhante a uma divis&atilde;o por grupos
ou natureza do registro/lan&ccedil;amento, ou seja, uma forma de
categoriza&ccedil;&atilde;o para futura consulta/relat&oacute;rio.</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.04cm; margin-bottom: 0cm">
<FONT FACE="Times, serif"><FONT SIZE=3>No cadastro de preced&ecirc;ncias
fica registrado a receita em que o d&eacute;bito ser&aacute; lan&ccedil;ado,
o hist&oacute;rico de c&aacute;lculo e em qual grupo de d&eacute;bito
vai estar inserido quando for efetuada a consulta na Consulta Geral
Financeira.</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.04cm; margin-bottom: 0cm">
<FONT FACE="Times, serif"><FONT SIZE=3>Na proced&ecirc;ncia fica
registrado tamb&eacute;m para qual receita ser&aacute; transferido o
d&eacute;bito no caso de efetuar um parcelamento.</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.04cm; margin-bottom: 0cm">
<BR>
</P>
<H3 CLASS="western"><A NAME="__RefHeading__1026_638646037"></A><A HREF="#Sum&aacute;rio1|region" NAME="db5197db"><FONT FACE="Times, serif"><FONT SIZE=3><SPAN STYLE="font-weight: normal">Inclus&atilde;o</SPAN></FONT></FONT></A></H3>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.04cm; margin-bottom: 0cm">
<BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.04cm; margin-bottom: 0cm">
<FONT FACE="Times, serif"><FONT SIZE=3>Para realizar o cadastro de
uma proced&ecirc;ncia, devem ser preenchidos os seguintes campos:</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><BR><BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY><FONT FACE="Times, serif"><FONT SIZE=3><I><SPAN STYLE="font-weight: normal">Proced&ecirc;ncia
- </SPAN></I>Esse campo corresponde ao c&oacute;digo do registro que
est&aacute; sendo inserido e ser&aacute; preenchido automaticamente
quando efetivar a inclus&atilde;o.</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY><FONT FACE="Times, serif"><FONT SIZE=3><I><SPAN STYLE="font-weight: normal">Descri&ccedil;&atilde;o
abreviada - </SPAN></I>Digite uma descri&ccedil;&atilde;o de forma
sucinta que identifique o cadastro que est&aacute; sendo efetuado.
Esse campo permite uma descri&ccedil;&atilde;o com at&eacute; 20
d&iacute;gitos.</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><FONT FACE="Times, serif"><FONT SIZE=3><I><SPAN STYLE="font-weight: normal">Descri&ccedil;&atilde;o
Completa - </SPAN></I>Digite uma descri&ccedil;&atilde;o completa que
identifique o cadastro que est&aacute; sendo efetuado. Esse campo
permite uma descri&ccedil;&atilde;o com at&eacute; 40 d&iacute;gitos.</FONT></FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm">Receita -
<FONT FACE="Times, serif"><FONT SIZE=3>Informe o c&oacute;digo da
receita correspondente &agrave; proced&ecirc;ncia de d&eacute;bito
que est&aacute; sendo</FONT></FONT> cadastrada. Caso n&atilde;o saiba
o c&oacute;digo, clique no link para pesquisar pelo nome da receita. 
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm">	Esse
c&oacute;digo corresponde a qual receita or&ccedil;ament&aacute;ria
ser&aacute; lan&ccedil;ado o d&eacute;bito quando for efetuada a
inclus&atilde;o de um diverso e em qual receita ser&aacute;
contabilizada a arrecada&ccedil;&atilde;o quando for efetuado o
pagamento desse d&eacute;bito.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><I><SPAN STYLE="font-weight: normal">Hist&oacute;rico
de C&aacute;lculo - </SPAN></I>Informe o c&oacute;digo do hist&oacute;rico
de c&aacute;lculo correspondente ao tipo de d&eacute;bito.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm">	Esse
hist&oacute;rico tamb&eacute;m serve como forma de identifica&ccedil;&atilde;o
da parcela do d&eacute;bito na Consulta Geral Financeira.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><B>	Ex.:</B>
Diversos, loteamentos, etc.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><I><SPAN STYLE="font-weight: normal">Tipo
de D&eacute;bito - </SPAN></I>Informe o c&oacute;digo do tipo de
d&eacute;bito que deseja que os d&eacute;bitos que forem cadastrados
nessa proced&ecirc;ncia sejam agrupados quando forem consultados na
Consulta Geral Financeira.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 2.04cm; margin-bottom: 0cm">
<B>Ex.:</B> Diversos</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><I>Data
Limite:</I> Neste campo poder&aacute; informar a data limite para
utiliza&ccedil;&atilde;o desta proced&ecirc;ncia. Quando esta data
estiver vencida, esta proced&ecirc;ncia n&atilde;o ser&aacute; mais
mostrada para lan&ccedil;ar d&eacute;bitos.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm"><BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm">	Ap&oacute;s
lan&ccedil;ar as informa&ccedil;&otilde;es descritas nos campos
acima, clique no bot&atilde;o &ldquo;incluir&rdquo;. Automaticamente
ser&aacute; ativada a aba para informar a receita para parcelamento
do diverso.</P>
