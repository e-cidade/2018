	Social</U></FONT>&rdquo; para pesquisar pelo nome.</P>
	<LI><P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm">Vincular
	a uma matr&iacute;cula de im&oacute;vel - preencha o respectivo
	campo com o c&oacute;digo da matr&iacute;cula ou clique no link
	&ldquo;<FONT COLOR="#0000ff"><U>Matr&iacute;cula do Im&oacute;vel</U></FONT>&rdquo;
	para pesquisar pelo nome do propriet&aacute;rio, setor, quadra,
	lote, etc.</P>
	<LI><P CLASS="western" ALIGN=JUSTIFY STYLE="margin-bottom: 0cm">Vincular
	a uma inscri&ccedil;&atilde;o municipal - preencha o respectivo
	campo com o c&oacute;digo da inscri&ccedil;&atilde;o ou  clique no
	link &ldquo;<FONT COLOR="#0000ff"><U>Inscri&ccedil;&atilde;o
	Municipal</U></FONT>&rdquo; para pesquisar pelo nome da Empresa ou
	profissional aut&ocirc;nomo do cadastro de alvar&aacute;s.</P>
</UL>
